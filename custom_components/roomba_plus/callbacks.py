# AUTO-ADVANCE-ROOM time-confidence threshold: time spent in the current room
# must be at least this fraction of the heuristic expected-per-room duration
# before a transient non-run phase is trusted as a genuine room completion.
# Conservative (0.5) since expected_room_sec is a uniform-split heuristic, not
# a true per-room estimate — large rooms legitimately take longer than average.
_ROOM_TRANSITION_MIN_ELAPSED_RATIO: float = 0.5


def _room_transition_confidence_ok(
    mission: dict[str, Any],
    mts: Any,
) -> bool:
    """AUTO-ADVANCE-ROOM (v2.8.0) confidence check.

    Combines four signals (per community feature request from Thonno) to
    decide whether a transient non-run cleanMissionStatus phase represents a
    genuine inter-room transition, as opposed to an error, a user pause, or a
    genuine low-battery recharge that happens to pass through the same phase
    string ("charge").

    Conditions (ALL must hold):
      1. No active error on the mission (mission.error == 0).
      2. cycle is still "clean" or "quick" — robot has not reported mission end.
      3. expected_room_sec is available AND time_in_current_room_sec is at
         least _ROOM_TRANSITION_MIN_ELAPSED_RATIO of the expected per-room
         duration.

    v2.8.0 (live wiring): the call site in this module now computes real
    per-room cloud TE1 time estimates via _compute_room_time_estimates() and
    passes them through set_mission_plan(room_estimates_sec=...), so
    expected_room_sec is populated whenever cloud data is available for the
    current room (falling back to a uniform total_estimated_sec / room_count
    split otherwise). AUTO-ADVANCE-ROOM is therefore live, not dormant, as
    long as at least a uniform estimate can be derived.

    Condition 2 shares the same cycle field as the INTER-ROOM-TRANSITION
    guard above and is subject to the same known reliability caveat — see
    the v2.8.1 END-DEBOUNCE comment on that guard. A cycle misread here would
    simply suppress an auto-advance (conservative failure mode) rather than
    corrupt state, so no debounce was added on this path.
    """
    error_code = mission.get("error", 0) or 0
    if error_code:
        _LOGGER.debug(
            "AUTO-ADVANCE-ROOM: confidence check failed — "
            "active error_code=%d",
            error_code,
        )
        return False

    cycle = mission.get("cycle", "")
    if cycle not in ("clean", "quick"):
        _LOGGER.debug(
            "AUTO-ADVANCE-ROOM: confidence check failed — "
            "cycle=%r not in (clean, quick)",
            cycle,
        )
        return False

    expected = mts.expected_room_sec
    if expected is None or expected <= 0:
        _LOGGER.debug(
            "AUTO-ADVANCE-ROOM: confidence check failed — "
            "expected_room_sec=%s (no per-room or total estimate available)",
            expected,
        )
        return False  # cannot confirm timing — conservative refusal (see docstring)

    elapsed = mts.time_in_current_room_sec
    _ok = elapsed >= expected * _ROOM_TRANSITION_MIN_ELAPSED_RATIO
    if not _ok:
        _LOGGER.debug(
            "AUTO-ADVANCE-ROOM: confidence check failed — "
            "elapsed=%.1fs < expected=%.1fs * ratio=%.2f (room finished "
            "faster than estimated, or robot just entered it)",
            elapsed, expected, _ROOM_TRANSITION_MIN_ELAPSED_RATIO,
        )
    return _ok


# ── Partial-message-safe top-level key lookup ───────────────────────────────

def _merged_top_level(
    entry: RoombaConfigEntry,
    reported: dict[str, Any],
    key: str,
) -> dict[str, Any]:
    """Return reported[key], falling back to the cached master_state's
    same key when this message's `reported` doesn't carry it at all.

    MQTT messages are deltas — a message that updates cleanMissionStatus
    (e.g. a phase transition) is not guaranteed to also carry bbrun,
    runtimeStats, bbchg3, pose, or lastCommand, since those are separate
    top-level keys. Reading reported.get(key, {}) directly on exactly the
    message that triggered a phase-transition handler silently substitutes
    an empty/zero default for the robot's actual last-known value.

    Harmless for "this round only" reads that get a fresh chance on the
    next message. Wrong for BASELINE/SNAPSHOT reads — nStuck-at-start,
    zone names captured at mission start, or per-mission analytics
    recorded exactly once at mission end — where a wrong default becomes
    a permanent, never-self-correcting error (e.g. a clean mission
    misclassified as stuck_and_resumed because nStuck's baseline was
    wrongly read as 0 instead of the robot's true lifetime count).
    """
    value = reported.get(key)
    if value:
        return value
    roomba = getattr(entry.runtime_data, "roomba", None)
    master = getattr(roomba, "master_state", None) or {}
    return ((master.get("state") or {}).get("reported") or {}).get(key) or {}


# ── Zone-name capture ─────────────────────────────────────────────────────────

def _capture_zone_names(
    entry: RoombaConfigEntry,
    reported: dict[str, Any],
) -> list[str]:
    """Resolve zone names at mission start for all robot types.

    ROOM-SEG Stage 6 — EPHEMERAL branch backed by RoomSegStore, not
    ZoneStore (deleted — gap heuristic proved unreliable, see
    ROOM_SEGMENTATION_NOTES.md).
    """
    from .models import MapCapability

    data: RoombaData = entry.runtime_data
    if data.room_seg_store:                      # EPHEMERAL
        return [r.name for r in data.room_seg_store.rooms.values() if r.confirmed]
    if data.map_capability == MapCapability.SMART:
        last_cmd = _merged_top_level(entry, reported, "lastCommand")
        region_ids = [
            r.get("region_id")
            for r in (last_cmd.get("regions") or [])
            if r.get("region_id")
        ]
        if region_ids and data.cloud_coordinator:
            id_to_name = {
                r["id"]: r["name"]
                for r in data.cloud_coordinator.regions
                if r.get("id")
            }
            return [id_to_name[rid] for rid in region_ids if rid in id_to_name]
    return []  # NONE (600-series) or SMART without cloud


# ── Mission record builder ────────────────────────────────────────────────────

async def async_record_mission(
    hass: Any,
    entry: RoombaConfigEntry,
    mission: dict[str, Any],
    reported: dict[str, Any],
    zones: list[str],
    start_ts: int,
    nstuck_delta: int,
    recharge_min: int = 0,
    result_override: str | None = None,
    npicks_delta: int = 0,
) -> None:
    """Build and append a mission record to MissionStore.

    Args:
        hass:            HomeAssistant instance (for storage and dt_util).
        entry:           Config entry whose runtime_data holds the MissionStore.
        mission:         cleanMissionStatus dict from the end-of-mission MQTT message.
        reported:        Full reported state dict from the same MQTT message.
        zones:           Zone names captured at mission START (not end).
        start_ts:        mssnStrtTm cached at mission START. The 980/900-series
                         firmware resets this field to 0 in the end MQTT message,
                         so it must be captured when the cleaning phase begins.
        nstuck_delta:    bbrun.nStuck delta since mission start. Using the delta
                         rather than the lifetime counter prevents marking every
                         mission as stuck when bbrun arrives in the same packet.
        npicks_delta:    v3.2.0 ANOMALY-EXPLAIN — bbrun.nPicks delta since mission
                         start, same delta-not-lifetime-counter reasoning as
                         nstuck_delta. >0 means the robot was physically picked
                         up during this mission. Unlike nstuck_delta (only used
                         to derive `result`), this IS stored directly on the
                         record — there's no other field it could be inferred
                         from later.
        recharge_min:    F4e — minutes spent recharging mid-mission, accumulated
                         from rechrgM fields across hmMidMsn phase entries.
        result_override: F6c — when set, overrides the computed result string
                         (e.g. "blocked_timeout" from BlockingManager).
    """
    data: RoombaData = entry.runtime_data
    if data.mission_store is None:
        return

    now = dt_util.utcnow()

    if start_ts:
        started_at = datetime.datetime.fromtimestamp(
            start_ts, datetime.timezone.utc
        )
    else:
        # Fallback: wall-clock at recording time (duration will be ~0).
        # Should not be reached after the closure fix — log for diagnostics.
        started_at = now
        _LOGGER.warning(
            "MissionStore: no cached start_ts for mission — duration will be 0"
        )

    phase = mission.get("phase", "")
    error_code: int = mission.get("error", 0) or 0

    # F6c — result_override takes precedence (e.g. "blocked_timeout")
    if result_override:
        result = result_override
    elif error_code:
        result = "error"
    elif phase == "cancelled":
        result = "cancelled"
    elif nstuck_delta > 0:
        # F6h — default to stuck_and_abandoned; caller may override to
        # stuck_and_resumed if the phase returned to "run" after the stuck event.
        result = "stuck"
    else:
        result = "completed"

    elapsed_sec = (now - started_at).total_seconds()
    duration_min = max(0, round(elapsed_sec / 60))

    # Merge bbrun + runtimeStats — mirrors entity.py run_stats.
    # On i-series (lewis firmware) hr lives in runtimeStats, not bbrun.
    bbrun = _merged_top_level(entry, reported, "bbrun")
    runtime = _merged_top_level(entry, reported, "runtimeStats")
    bbrun_hr = bbrun.get("hr") or runtime.get("hr") or 0

    # v2.9.0 DAILY-DIGEST — chemistry-aware lifetime charge-cycle snapshot at
    # mission end, mirrors bbrun_hr above: a per-mission absolute-counter
    # snapshot, not a delta. api_views.py computes the cross-day delta the
    # same way it already does for bbrun_hr (filter_hours_today). None when
    # bbchg3 can be entirely absent (confirmed on 600-series AND on a j7+
    # running sapphire firmware — not a 600-series-exclusive phenomenon,
    # see MISSIONSTORE_FIELD_REGISTRY.md's raw vacuum_state findings).
    battery_cycles = active_charge_cycles(_merged_top_level(entry, reported, "bbchg3"))

    # F8b — capture error context at mission end
    # pose is None for firmware 3.20+ and 600-series; absent when null.
    error_position_mm: dict | None = None
    if error_code:
        _pose = _merged_top_level(entry, reported, "pose")
        pose_point = _pose.get("point") if _pose else None
        if pose_point:
            try:
                # v2.9.0 — firmware reports cm, not mm. See POSE_POINT_CM_TO_MM
                # in const.py.
                error_position_mm = {
                    "x": float(pose_point.get("x", 0)) * POSE_POINT_CM_TO_MM,
                    "y": float(pose_point.get("y", 0)) * POSE_POINT_CM_TO_MM,
                }
            except (TypeError, ValueError):
                pass

    phase_at_error: str | None = phase if error_code else None

    self_recovered: bool | None = (
        True  if result == "stuck_and_resumed"   else
        False if result == "stuck_and_abandoned" else
        None
    )

    # v3.2.0 TEAM-INDICATOR — was this mission part of an Imprint Link
    # team clean? lastCommand.params.team.team_id is only present when the
    # mission was started as part of a multi-robot team routine (confirmed
    # via KingAntDesigns' j7+ field data — three scheduled team routines +
    # the actual last mission run). Purely informational; None on ordinary
    # single-robot missions, which is the vast majority.
    last_cmd_for_team = _merged_top_level(entry, reported, "lastCommand")
    team_id: str | None = (
        (last_cmd_for_team.get("params") or {}).get("team") or {}
    ).get("team_id")

    record: dict[str, Any] = {
        "id": f"m_{int(started_at.timestamp())}",
        "started_at": started_at.isoformat(),
        "ended_at": now.isoformat(),
        "duration_min": duration_min,
        # v2.8.2: "or None" added — some robots (confirmed: 980 9-series with
        # aftermarket NiMH battery) report cleanMissionStatus.sqft as a literal
        # 0 rather than omitting the field, even for missions lasting hours.
        # Without this, the merge-precedence rule ("never overwrite a
        # non-None value") permanently locks out cloud backfill, even though
        # the cloud often has a real area value for the same mission. A
        # genuine 0-sqft mission is indistinguishable from "no data" anyway,
        # so this loses nothing and lets backfill work as intended.
        "area_sqft": mission.get("sqft") or None,   # None for 600-series — correct
        "result": result,
        "initiator": mission.get("initiator", "none"),
        "zones": zones,
        "error_code": error_code if error_code else None,
        "bbrun_hr": bbrun_hr,
        "battery_cycles": battery_cycles,   # v2.9.0 DAILY-DIGEST
        "recharge_min": recharge_min if recharge_min > 0 else None,  # F4e
        # F8b — error context fields (None when no error or no pose data)
        "error_position_mm": error_position_mm,
        "phase_at_error":    phase_at_error,
        "self_recovered":    self_recovered,
        "team_id": team_id,   # v3.2.0 TEAM-INDICATOR
        "npicks_delta": npicks_delta,   # v3.2.0 ANOMALY-EXPLAIN
    }

    await data.mission_store.async_append(record)
    await data.mission_store.async_save(hass, entry.entry_id)

    # v3.1.0 L9-MAP — update the personal relocalisation baseline.
    # Same gate as the MissionStore record itself (this function only runs
    # when had_cleaning_phase was True, i.e. a genuine cleaning mission
    # occurred) — avoids polluting the baseline with the automatic nMssn
    # increments that happen at "mission ready" without any actual cleaning
    # (field-confirmed by Thonno: mssnNavStats resets to near-zero values on
    # those pseudo-missions, which would corrupt a baseline meant to reflect
    # real navigation behaviour).
    rps = getattr(data, "robot_profile_store", None)
    if rps is not None:
        nav_stats = reported.get("mssnNavStats")
        if isinstance(nav_stats, dict) and "reLc" in nav_stats:
            try:
                relocs = int(nav_stats["reLc"])
            except (TypeError, ValueError):
                relocs = None
            if relocs is not None:
                rps.update_reloc_baseline(relocs)
                await rps.async_save(hass, entry.entry_id)
                from .repairs import async_check_reloc_alert
                async_check_reloc_alert(hass, entry)

        # v3.1.0 L9-BATTERY — record this mission's estCap observation into
        # the noise-floor baseline. Same had_cleaning_phase gate as above —
        # a consistent per-mission cadence is what the noise-floor baseline
        # is meant to characterise; recording on every MQTT update instead
        # would mix in mid-mission jitter unrelated to charge-cycle deltas.
        bbchg3 = _merged_top_level(entry, reported, "bbchg3")
        raw_estcap = bbchg3.get("estCap")
        if raw_estcap:
            profile = data.robot_profile
            if profile is not None:
                estcap_mah = estcap_to_mah(
                    raw_estcap,
                    profile.estcap_scale_liion,
                    profile.estcap_scale_nimh,
                    bbchg3.get("nNimhChrg"),
                )
                if estcap_mah is not None:
                    rps.record_estcap_observation(estcap_mah)
                    await rps.async_save(hass, entry.entry_id)

    # v2.9.0 EVENT-BUS — fire roomba_plus_mission_completed for automations.
    # entry_id + name included (unlike the older L6 presence events) so
    # multi-robot installs can filter without a separate lookup.
    #
    # v3.2.0 UX fix — the ANOMALY-EXPLAIN result is folded directly into
    # the payload instead of requiring the user to (a) know the
    # explain_mission service exists and (b) call it themselves after
    # every mission. The record was appended above, so explain_mission()
    # sees it; the computation is once-per-mission-end, negligible.
    # Payload shape is CONSTANT: the three explanation fields are always
    # present (null when not anomalous) so automation templates never
    # have to guard against missing keys depending on the mission.
    explanation = data.mission_store.explain_mission(record["id"]) or {}
    hass.bus.async_fire(
        EVENT_MISSION_COMPLETED,
        {
            "entry_id": entry.entry_id,
            "name": entry.title,
            "rooms_cleaned": len(zones),
            "area_sqft": record["area_sqft"],
            "stuck_count": nstuck_delta,
            "result": result,
            "is_anomalous": bool(explanation.get("is_anomalous", False)),
            "anomaly_reason": explanation.get("anomaly_reason"),
            "recommended_action": explanation.get("recommended_action"),
            "robot_lifted": bool(explanation.get("robot_lifted", False)),
        },
    )


    # F6g — reset consecutive skip counter on any successful completion
    if result == "completed" and data.maintenance_store is not None:
        if data.maintenance_store.consecutive_skips > 0:
            data.maintenance_store.consecutive_skips = 0
            _LOGGER.debug("MissionStore: consecutive_skips reset on completed mission")
            await data.maintenance_store.async_save(hass, entry.entry_id)

    # F6e — check for mixed schedule patterns after each mission
    # Guard: only fire when hass is fully initialised (has config attribute)
    if getattr(hass, "is_running", False):
        from .repairs import async_check_mixed_schedule, async_check_mission_anomaly
        asyncio.run_coroutine_threadsafe(
            async_check_mixed_schedule(hass, entry), hass.loop
        )
        # L3 — check for consecutive anomalous missions after each append
        asyncio.run_coroutine_threadsafe(
            async_check_mission_anomaly(hass, entry), hass.loop
        )

    # Update L3 runtime error state.
    # Only update on error/stuck — do NOT clear on completed so the last
    # error remains visible until the next error occurs.
    if result in ("error", "stuck", "stuck_and_resumed", "stuck_and_abandoned"):
        data.last_error_code = error_code if error_code else None
        data.last_error_at   = now.isoformat()
        data.last_error_zone = zones[0] if zones else None

    _LOGGER.debug(
        "MissionStore: recorded %s result=%s duration=%dmin zones=%s",
        record["id"], result, duration_min, zones,
    )


