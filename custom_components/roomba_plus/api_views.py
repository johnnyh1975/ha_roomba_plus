_VALID_FORMATS = {"summary", "records", "hazards", "export", "zone_coverage_health"}


def _build_local_zones_index(mission_store_records: list[dict]) -> dict[int, list[str]]:
    """Build a ts -> zone-names index from local MissionStore records.

    F4a -- used to inject zone names into cloud records which carry zones=[].
    Keys are integer Unix timestamps of ended_at. Tolerance matching is done
    at the call site (120 s window).
    """
    index: dict[int, list[str]] = {}
    for r in mission_store_records:
        ended_str = r.get("ended_at", "")
        zones = r.get("zones") or []
        if not ended_str or not zones:
            continue
        try:
            ended_dt = datetime.datetime.fromisoformat(ended_str)
        except (ValueError, TypeError):
            continue
        if ended_dt.tzinfo is None:
            ended_dt = ended_dt.replace(tzinfo=datetime.timezone.utc)
        index[int(ended_dt.timestamp())] = list(zones)
    return index


def _inject_zones(
    record: dict[str, Any],
    local_zones_index: dict[int, list[str]],
    tolerance_sec: int = 120,
) -> dict[str, Any]:
    """Return record with zones populated from local MissionStore when available.

    F4a -- matches on the cloud record's ended_at ISO string against the
    local ended_at index within tolerance_sec.  If a match is found and the
    local record has zone names, they replace the empty zones list.
    """
    end_ts_str = record.get("ended_at")
    if not end_ts_str or not local_zones_index:
        return record

    try:
        end_dt = datetime.datetime.fromisoformat(end_ts_str)
        if end_dt.tzinfo is None:
            end_dt = end_dt.replace(tzinfo=datetime.timezone.utc)
        end_ts_int = int(end_dt.timestamp())
    except (ValueError, TypeError):
        return record

    best_ts = min(local_zones_index, key=lambda t: abs(t - end_ts_int), default=None)
    if best_ts is not None and abs(best_ts - end_ts_int) <= tolerance_sec:
        zones = local_zones_index[best_ts]
        if zones:
            return {**record, "zones": zones}
    return record


def _safe_int(value: Any) -> int | None:
    """v3.2.1 — int-or-null for unified-record fields.

    nMssn arrives as int from cloud_coordinator's type-narrowing, but the
    import endpoint and pre-v3.2.0 stores may hold strings or garbage —
    the converters must never raise on a single bad record.
    """
    try:
        return int(value) if value is not None else None
    except (TypeError, ValueError):
        return None


def _cloud_record_to_unified(record: dict[str, Any]) -> dict[str, Any]:
    """Convert a raw cloud /missionhistory record to the unified per-mission shape."""
    start_ts = record.get("startTime")
    end_ts   = record.get("timestamp")

    started_at = (
        datetime.datetime.fromtimestamp(int(start_ts), tz=datetime.timezone.utc).isoformat()
        if start_ts else None
    )
    ended_at = (
        datetime.datetime.fromtimestamp(int(end_ts), tz=datetime.timezone.utc).isoformat()
        if end_ts else None
    )

    duration_min = int(record.get("durationM") or record.get("doneM") or 0)
    run_min      = record.get("runM")
    pause_id     = record.get("pauseId")
    try:
        pause_id_int = int(pause_id) if pause_id is not None else 0
    except (TypeError, ValueError):
        pause_id_int = 0
    error_code = pause_id_int if pause_id_int > 0 else None

    return {
        "id":           f"c_{start_ts}" if start_ts else f"c_{end_ts}",
        "started_at":   started_at,
        "ended_at":     ended_at,
        "duration_min": duration_min,
        "run_min":      int(run_min) if run_min is not None else None,
        "area_sqft":    record.get("sqft"),
        "result":       record.get("classified_result", "unknown"),
        "initiator":    record.get("initiator", "none"),
        "zones":        [],   # F4a: populated by _inject_zones() below
        "error_code":   error_code,
        "recharges":    record.get("chrgs"),
        "evacuations":  record.get("evacs"),
        "dirt_events":  record.get("dirt"),
        "wifi_signal":  record.get("wlBars"),
        "source":       "cloud",
        # v2.3.0 — from CR4 timeline merge; alignment_confidence injected in get()
        "room_coverage":        record.get("room_coverage"),
        "alignment_confidence": None,
        # v3.2.1 — lifetime mission counter, the key of MissionPathView
        # (/mission/{n_mssn}/path).  Unblocks card F4 (path replay): the
        # card needs it per-row to build the replay request.  Every raw
        # cloud record carries nMssn (see cloud_coordinator.py).
        "n_mssn":       _safe_int(record.get("nMssn")),
    }


def _local_record_to_unified(record: dict[str, Any]) -> dict[str, Any]:
    """Convert a local MissionStore record to the unified per-mission shape.

    After v2.1.3 CR1/CR2: dirt_events, wifi_signal, and evacuations are
    populated when the record has been enriched by backfill_from_cloud()
    (v2.9.0 — now called on every cloud refresh, not just at HA startup).
    recharges stays None (no local accumulator).
    source remains "local" — reflects origin, not field provenance.
    """
    return {
        "id":           record.get("id", ""),
        "started_at":   record.get("started_at"),
        "ended_at":     record.get("ended_at"),
        "duration_min": record.get("duration_min"),
        "run_min":      None,                        # local source never has runM
        "area_sqft":    record.get("area_sqft"),
        "result":       record.get("result", "unknown"),
        "initiator":    record.get("initiator", "none"),
        "zones":        record.get("zones", []),
        "error_code":   record.get("error_code"),
        "recharges":    None,                        # no local accumulator
        "evacuations":  record.get("evacs"),         # from CR1 merge
        "dirt_events":  record.get("dirt"),          # from CR1 merge
        "wifi_signal":  record.get("wlBars"),        # from CR1 merge
        "source":       "local",
        # v2.3.0 — from CR4 timeline merge; alignment_confidence always null for local
        "room_coverage":        record.get("room_coverage"),
        "alignment_confidence": None,
        # v3.2.1 — card F4 (path replay), see _cloud_record_to_unified.
        # Local records never get nMssn at creation (callbacks.py); it
        # arrives via backfill_from_cloud() (_CLOUD_MERGE_SCALAR) — null
        # until enriched, which the card treats as "replay unavailable".
        "n_mssn":       _safe_int(record.get("nMssn")),
    }


def _local_record_has_cloud_merge_signal(record: dict[str, Any]) -> bool:
    """True if this local MissionStore record was ever successfully
    matched against a cloud record by backfill_from_cloud() or
    merge_latest_from_cloud() — i.e. at least one cloud-only analytics
    field landed on it.

    v2.10.2 (RECORDS-UNION) — same heuristic cloud_coordinator.py's CR3
    fallback already uses to identify enriched local records, inverted
    here to find records that NEVER matched any cloud entry. A genuine
    mismatch on one field while the others stayed null is not expected
    in practice (all three are written together by the same merge
    call), so this is a reliable proxy for "cloud never knew about
    this mission" without needing new matching logic.
    """
    return any(record.get(f) is not None for f in ("dirt", "chrgM", "wlBars"))


class MissionHistoryView(HomeAssistantView):
    """GET /api/roomba_plus/{entry_id}/mission_history"""

    url = "/api/roomba_plus/{entry_id}/mission_history"
    name = "api:roomba_plus:mission_history"
    requires_auth = True

    async def get(self, request: web.Request, entry_id: str) -> web.Response:
        hass: HomeAssistant = request.app["hass"]
        entry = hass.config_entries.async_get_entry(entry_id)
        if entry is None or entry.domain != DOMAIN:
            return self.json_message("Entry not found", status_code=404)

        # F7o -- startup race guard: runtime_data may not be ready immediately
        # after HA start if a request arrives before setup completes.
        if not hasattr(entry, "runtime_data") or entry.runtime_data is None:
            return self.json_message("Integration initialising", status_code=503)

        data: RoombaData = entry.runtime_data

        fmt = request.query.get("format", "summary").strip().lower()

        # F7o -- reject unknown format values with a clear 400
        if fmt not in _VALID_FORMATS:
            return self.json_message(
                f"Unknown format '{fmt}'. Valid values: {', '.join(sorted(_VALID_FORMATS))}",
                status_code=400,
            )

        # -- format=summary (default, backward compatible) --------------------
        if fmt == "summary":
            if data.mission_store is None:
                return self.json([], status_code=200)
            try:
                days = int(request.query.get("days", 28))
                days = max(1, min(days, 90))
            except (ValueError, TypeError):
                days = 28

            by_day = data.mission_store.query_by_day(days)
            # F12b — P4: per-day dirt density now cached on coordinator after each
            # cloud fetch; read directly instead of re-iterating records here.
            _p30d_baseline: float | None = None
            _daily_density: dict[str, float] = {}
            if data.has_cloud and data.cloud_coordinator is not None:
                _daily_density = data.cloud_coordinator.daily_dirt_density
                # 30-day baseline: median of all cached per-day medians
                all_densities = list(_daily_density.values())
                if len(all_densities) >= 5:
                    import statistics as _stat
                    _p30d_baseline = _stat.median(all_densities)

            # L5: per-room relative dirtiness from RobotProfileStore
            _room_dirt_relative: dict[str, float] | None = None
            _rps = getattr(data, "robot_profile_store", None)
            if _rps is not None:
                rd = _rps.room_dirt_relative()
                if rd:
                    _room_dirt_relative = rd

            result = [
                {
                    "date":               summary.date.isoformat(),
                    "total":              summary.total,
                    "completed":          summary.completed,
                    "stuck":              summary.stuck,
                    "area_sqft":          summary.area_sqft,
                    "result":             summary.result,
                    # F12b — dirt analytics read from coordinator cache
                    "dirt_density":       _daily_density.get(summary.date.isoformat()),
                    "relative_to_baseline": (
                        round(_daily_density[summary.date.isoformat()] / _p30d_baseline, 3)
                        if summary.date.isoformat() in _daily_density and _p30d_baseline
                        else None
                    ),
                    # L5 (v2.6.0) — per-room dirtiness index (once per day, not per-summary)
                    "room_dirt_index": _room_dirt_relative,
                }
                for summary in sorted(by_day.values(), key=lambda s: s.date)
            ]
            return self.json(result)

        # -- format=zone_coverage_health ---------------------------------------
        # v3.2.0 COVERAGE-FREQ — per-room self-calibrated cleaning-cadence
        # health. A distinct format value rather than a field bolted onto
        # format=summary's response: that endpoint returns a bare array
        # (the existing card already consumes it as such), and
        # zone_coverage_health is a per-room snapshot, not a per-day
        # metric — it doesn't have a natural slot inside a day-by-day
        # array without either breaking that array's existing shape or
        # duplicating the same room data onto every day entry.
        if fmt == "zone_coverage_health":
            if data.mission_store is None:
                return self.json({}, status_code=200)
            _region_map = {
                r["id"]: r["name"]
                for r in (getattr(data.cloud_coordinator, "regions", None) or [])
                if r.get("id")
            } if data.cloud_coordinator is not None else {}
            _umf = (
                data.umf_aligner.rid_to_name()
                if not _region_map and data.umf_aligner and data.umf_aligner.aligned
                else None
            )
            # v3.3.0 ROOM-SCHED foundation fix — derive rooms per record
            result = data.mission_store.room_coverage_health(
                dt_util.now().isoformat(), region_map=_region_map, umf_regions=_umf
            )
            return self.json(result)

        # -- format=hazards ---------------------------------------------------
        # F9 — returns GridStore stuck hotspots and UMF-seeded obstacle zones.
        if fmt == "hazards":
            from .models import MapCapability
            if data.map_capability == MapCapability.NONE or data.grid_store is None:
                return self.json([])
            hazards = data.grid_store.hotspots()
            # Append UMF-seeded observed zones (source="robot_learned")
            if data.has_cloud:
                import math
                for centroid in data.cloud_coordinator.observed_zone_centroids:
                    x = centroid.get("x") or 0.0
                    y = centroid.get("y") or 0.0
                    hazards.append({
                        "gx":          None,
                        "gy":          None,
                        "x_mm":        x,
                        "y_mm":        y,
                        "stuck_count": None,
                        "room_name":   None,
                        "bearing_deg": int(math.degrees(math.atan2(x, y)) % 360),
                        "distance_mm": int(math.sqrt(x ** 2 + y ** 2)),
                        "source":      "robot_learned",
                    })
                # v2.3.0 Step 8 / Gap B — keepout zone centroids
                for zone in data.cloud_coordinator.keepout_zones:
                    cx = zone.get("cx") or zone.get("centroid_x") or zone.get("x")
                    cy = zone.get("cy") or zone.get("centroid_y") or zone.get("y")
                    if cx is not None and cy is not None:
                        cx, cy = float(cx), float(cy)
                        hazards.append({
                            "gx":          None,
                            "gy":          None,
                            "x_mm":        cx,
                            "y_mm":        cy,
                            "stuck_count": None,
                            "room_name":   None,
                            "bearing_deg": int(math.degrees(math.atan2(cx, cy)) % 360),
                            "distance_mm": int(math.sqrt(cx ** 2 + cy ** 2)),
                            "source":      "keepout",
                        })

            # v2.3.0 Step 8 — populate room_name via UmfAligner
            aligner = getattr(data, "umf_aligner", None)
            if aligner and aligner.aligned:
                for hazard in hazards:
                    source   = hazard.get("source")
                    x_mm     = hazard["x_mm"]
                    y_mm     = hazard["y_mm"]
                    if source == "stuck_events":
                        pt_umf = aligner.pose_to_umf(x_mm, y_mm)
                        if pt_umf:
                            hazard["room_name"] = aligner.room_name_at(*pt_umf)
                    elif source in ("robot_learned", "keepout"):
                        hazard["room_name"] = aligner.room_name_at(x_mm, y_mm)

            return self.json(hazards)

        # -- format=export (F15) ----------------------------------------------
        # Returns a versioned JSON bundle of all MissionStore records for
        # backup / migration purposes.  Schema is frozen at version 1;
        # new fields added in later versions carry null defaults.
        if fmt == "export":
            records_out: list[dict] = []
            if data.mission_store is not None:
                records_out = list(data.mission_store.records)
            blid = entry.data.get("blid", "")
            return self.json({
                "export_version": 1,
                "exported_at": dt_util.utcnow().isoformat(),
                "blid": blid,
                "record_count": len(records_out),
                "records": records_out,
            })

        # -- format=records ---------------------------------------------------
        # F7o -- distinguish coordinator failure from genuinely empty data.
        if data.has_cloud and not data.cloud_coordinator.last_update_success:
            return self.json_message("Cloud coordinator unavailable", status_code=503)

        # Build local zone index for F4a injection into cloud records
        local_zones_index: dict[int, list[str]] = {}
        if data.mission_store is not None:
            local_zones_index = _build_local_zones_index(data.mission_store.records)

        if data.has_cloud and data.cloud_coordinator.raw_records:
            unified = [
                _cloud_record_to_unified(r)
                for r in data.cloud_coordinator.raw_records
            ]
            # F4a -- inject zone names from local store into cloud records
            if local_zones_index:
                unified = [_inject_zones(r, local_zones_index) for r in unified]
            # Cloud records are newest-first -- reverse to ascending for card
            records = list(reversed(unified))

            # v2.10.2 (RECORDS-UNION) -- a mission that completed locally
            # (real MQTT state transition) but never got a matching cloud
            # record -- for whatever reason on iRobot's side -- was
            # previously invisible here whenever the cloud was healthy,
            # even though format=summary's `total` (always local) counts
            # it. Confirmed in the field: total=3 for a day while this
            # endpoint, cloud-sourced, only had 2. Fill the gap with any
            # local record from the same window that never got a cloud
            # merge match (see _local_record_has_cloud_merge_signal).
            if data.mission_store is not None:
                try:
                    union_days = int(request.query.get("days", 90))
                    union_days = max(1, min(union_days, 90))
                except (ValueError, TypeError):
                    union_days = 90
                unmatched_local = [
                    _local_record_to_unified(r)
                    for r in data.mission_store.query(union_days)
                    if not _local_record_has_cloud_merge_signal(r)
                ]
                if unmatched_local:
                    records = sorted(
                        records + unmatched_local,
                        key=lambda r: r.get("started_at") or "",
                    )
        elif data.mission_store is not None:
            try:
                days = int(request.query.get("days", 90))
                days = max(1, min(days, 90))
            except (ValueError, TypeError):
                days = 90
            local = data.mission_store.query(days)
            records = [_local_record_to_unified(r) for r in local]
        else:
            records = []

        # v2.3.0 — inject alignment_confidence for cloud-source records
        aligner = getattr(data, "umf_aligner", None)
        if aligner and aligner.aligned:
            conf = round(aligner.confidence, 2)
            for r in records:
                if r.get("source") == "cloud":
                    r["alignment_confidence"] = conf

        return self.json(records)


