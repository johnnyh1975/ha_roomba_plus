class MissionPathView(HomeAssistantView):
    """GET /api/roomba_plus/{entry_id}/mission/{nMssn}/path

    v3.2.0 MISSION-REPLAY — room-granular post-hoc reconstruction of a
    mission's path: "Robot was in Kitchen at 09:05, Hallway at 09:23,
    Bedroom at 09:31." Not pixel-accurate pose tracking — built from
    MissionArchive.mission_path()'s collapsed room_visits (same data
    ROOM-ACCESS's traversal-efficiency signal already uses), so this
    needed far less new code than originally scoped: the underlying
    room_visits extraction already existed.

    Room-name resolution: cloud_coordinator.regions (SMART tier) or
    room_seg_store.rooms[rid].name (EPHEMERAL tier) — whichever the
    entry has available. Falls back to the raw rid when neither
    resolves a name for a given room.
    """

    url = "/api/roomba_plus/{entry_id}/mission/{n_mssn}/path"
    name = "api:roomba_plus:mission_path"
    requires_auth = True

    async def get(
        self, request: web.Request, entry_id: str, n_mssn: str
    ) -> web.Response:
        hass: HomeAssistant = request.app["hass"]
        entry = hass.config_entries.async_get_entry(entry_id)
        if entry is None or entry.domain != DOMAIN:
            return self.json_message("Entry not found", status_code=404)

        if not hasattr(entry, "runtime_data") or entry.runtime_data is None:
            return self.json_message("Integration initialising", status_code=503)

        data: RoombaData = entry.runtime_data
        archive = getattr(data, "mission_archive", None)
        if archive is None:
            return self.json_message("No mission archive available", status_code=404)

        try:
            n_mssn_int = int(n_mssn)
        except ValueError:
            return self.json_message("Invalid mission number", status_code=400)

        record = archive.find_by_nMssn(n_mssn_int)
        if record is None:
            return self.json_message("Mission not found", status_code=404)

        from .mission_archive import MissionArchive, _ts_to_iso
        room_visits = record.get("room_visits") or []
        path = MissionArchive.mission_path(room_visits)

        id_to_name: dict[str, str] = {}
        cc = data.cloud_coordinator
        if cc is not None and cc.regions:
            id_to_name = {
                r["id"]: r["name"] for r in cc.regions if r.get("id") and r.get("name")
            }
        elif getattr(data, "room_seg_store", None) is not None:
            id_to_name = {
                rid: room.name
                for rid, room in data.room_seg_store.rooms.items()
                if room.name
            }

        timeline = [
            {
                "room": id_to_name.get(step["rid"], step["rid"]),
                "time": _ts_to_iso(step["ts"]),
            }
            for step in path
        ]

        return self.json({"nMssn": n_mssn_int, "path": timeline})


class MissionHistoryImportView(HomeAssistantView):
    """POST /api/roomba_plus/{entry_id}/mission_history/import

    F16 — import a v1 export bundle produced by format=export.
    Deduplicates against existing records by id — no overwrites.
    Returns {"imported": N, "skipped": N, "errors": []}.
    """

    url = "/api/roomba_plus/{entry_id}/mission_history/import"
    name = "api:roomba_plus:mission_history_import"
    requires_auth = True

    async def post(self, request: web.Request, entry_id: str) -> web.Response:
        hass: HomeAssistant = request.app["hass"]
        entry = hass.config_entries.async_get_entry(entry_id)
        if entry is None or entry.domain != DOMAIN:
            return self.json_message("Entry not found", status_code=404)

        if not hasattr(entry, "runtime_data") or entry.runtime_data is None:
            return self.json_message("Integration initialising", status_code=503)

        data: RoombaData = entry.runtime_data
        if data.mission_store is None:
            return self.json_message("Mission store unavailable", status_code=503)

        try:
            body = await request.json()
        except Exception:
            return self.json_message("Invalid JSON body", status_code=400)

        if not isinstance(body, dict):
            return self.json_message("Body must be a JSON object", status_code=400)

        version = body.get("export_version")
        if version is None:
            return self.json_message(
                "Missing export_version field", status_code=400
            )
        if version != 1:
            return self.json_message(
                f"Unsupported export_version {version!r}. Only version 1 is supported.",
                status_code=400,
            )

        incoming: list[dict] = body.get("records", [])
        if not isinstance(incoming, list):
            return self.json_message("records must be a list", status_code=400)

        # Bug 4 fix: reject oversized payloads before allocating memory.
        # 2 × MAX_RECORDS is a generous ceiling — legitimate exports from a
        # single robot can contain at most MAX_RECORDS (365) entries.
        from .mission_store import MAX_RECORDS
        if len(incoming) > MAX_RECORDS * 2:
            return self.json_message(
                f"records list too large: {len(incoming)} entries "
                f"(maximum {MAX_RECORDS * 2})",
                status_code=400,
            )

        # v3.3.0 STORE-ENCAP — full-history dedup and MAX_RECORDS trimming
        # now live inside MissionStore.append_validated(); this endpoint
        # previously rebuilt both invariants ad hoc (the documented
        # tech-debt bug source).
        imported = 0
        skipped = 0
        errors: list[str] = []

        def _record_type_errors(rec: dict) -> str | None:
            """v3.2.0 full-review fix — field-TYPE validation, previously
            absent entirely: a record only needed an `id` to be accepted,
            so e.g. `\"ended_at\": 12345` (int instead of ISO string) was
            persisted as-is and then crashed room_coverage_health() — and
            every other consumer that feeds timestamps to
            dt_util.parse_datetime — on EVERY subsequent cloud-refresh
            cycle, permanently, since the poisoned record survives
            restarts. Reproduced directly before this fix. Validating
            once at the import gate beats making every one of the many
            store consumers individually defensive."""
            for key in ("started_at", "ended_at", "result", "initiator"):
                if key in rec and rec[key] is not None and not isinstance(rec[key], str):
                    return f"field {key!r} must be a string or null"
            for key in ("duration_min", "area_sqft", "error_code", "bbrun_hr"):
                if key in rec and rec[key] is not None and not isinstance(rec[key], (int, float)):
                    return f"field {key!r} must be a number or null"
            for key in ("zones", "last_cleaned_rooms"):
                if key in rec and rec[key] is not None:
                    if not isinstance(rec[key], list) or not all(
                        isinstance(z, str) for z in rec[key]
                    ):
                        return f"field {key!r} must be a list of strings or null"
            return None

        for i, rec in enumerate(incoming):
            if not isinstance(rec, dict):
                errors.append(f"Record at index {i} is not a dict — skipped")
                skipped += 1
                continue
            rec_id = rec.get("id")
            if not rec_id or not isinstance(rec_id, str):
                errors.append(f"Record at index {i} has no string id field — skipped")
                skipped += 1
                continue
            type_error = _record_type_errors(rec)
            if type_error is not None:
                errors.append(f"Record {rec_id!r}: {type_error} — skipped")
                skipped += 1
                continue
            if not data.mission_store.append_validated(rec):
                skipped += 1  # duplicate id anywhere in the full history
                continue
            imported += 1

        if imported > 0:
            await data.mission_store.async_save(hass, entry_id)
            _LOGGER.info(
                "MissionHistoryImport: imported %d new record(s) for %s",
                imported, entry_id,
            )

        return self.json({
            "imported": imported,
            "skipped": skipped,
            "errors": errors,
        })


class HouseholdSummaryView(HomeAssistantView):
    """GET /api/roomba_plus/household?days=28

    F10b — aggregates all roomba_plus config entries. Returns per-robot
    breakdown with floor label, per-floor breakdown when labels are configured,
    and combined totals.
    """

    url = "/api/roomba_plus/household"
    name = "api:roomba_plus:household"
    requires_auth = True

    async def get(self, request: web.Request) -> web.Response:
        hass: HomeAssistant = request.app["hass"]

        try:
            days = int(request.query.get("days", 28))
            days = max(1, min(days, 90))
        except (ValueError, TypeError):
            days = 28

        entries = hass.config_entries.async_entries(DOMAIN)
        robots: list[dict] = []
        total_missions = 0
        total_completed = 0
        total_area: float | None = None
        floors: dict[str, dict] = {}

        for entry in entries:
            if not hasattr(entry, "runtime_data") or entry.runtime_data is None:
                continue
            data = entry.runtime_data
            if data.mission_store is None:
                continue

            by_day = data.mission_store.query_by_day(days)
            missions   = sum(s.total     for s in by_day.values())
            completed  = sum(s.completed for s in by_day.values())
            area_vals  = [s.area_sqft   for s in by_day.values()
                          if s.area_sqft is not None]
            area: float | None = sum(area_vals) if area_vals else None
            comp_pct   = round(100 * completed / missions, 1) if missions else 0.0

            floor_label = getattr(data, "floor_label", "") or ""

            robots.append({
                "entry_id":       entry.entry_id,
                "name":           entry.title or "Roomba",
                "floor":          floor_label,
                "missions":       missions,
                "completed":      completed,
                "completion_pct": comp_pct,
                "area_sqft":      area,
            })
            total_missions  += missions
            total_completed += completed
            if area is not None:
                total_area = (total_area or 0.0) + area

            if floor_label:
                f = floors.setdefault(floor_label, {
                    "label": floor_label, "missions": 0,
                    "completed": 0, "area_sqft": None,
                })
                f["missions"]  += missions
                f["completed"] += completed
                if area is not None:
                    f["area_sqft"] = (f["area_sqft"] or 0.0) + area

        total_comp_pct = (
            round(100 * total_completed / total_missions, 1)
            if total_missions else 0.0
        )

        result: dict[str, Any] = {
            "period_days": days,
            "total": {
                "missions":       total_missions,
                "completed":      total_completed,
                "completion_pct": total_comp_pct,
                "area_sqft":      total_area,
            },
            "robots": robots,
        }
        if floors:
            result["floors"] = list(floors.values())

        return self.json(result)


class DailyDigestView(HomeAssistantView):
    """GET /api/roomba_plus/{entry_id}/digest?date=2026-06-16

    v2.9.0 DAILY-DIGEST — compact one-day summary for the card's "Today"
    status slot. ``date`` defaults to today (robot's local timezone) when
    omitted.

    Response shape:
        {"missions": 2, "area_m2": 72.1, "stuck_events": 0,
         "demand_cleans": 1, "filter_hours_today": 1.4,
         "battery_cycles_today": 2}

    filter_hours_today / battery_cycles_today are TRUE day-attributable
    deltas of the respective lifetime counters (bbrun_hr / battery_cycles,
    both absolute snapshots captured per-mission in MissionStore), not a
    proxy — see _lifetime_delta_for_day() below. Both are None when there
    is no prior record to diff against (e.g. the robot's very first day of
    recorded history) rather than guessing a 0 baseline, which could silently
    misreport an unknown amount as "zero used today".
    """

    url = "/api/roomba_plus/{entry_id}/digest"
    name = "api:roomba_plus:digest"
    requires_auth = True

    async def get(self, request: web.Request, entry_id: str) -> web.Response:
        hass: HomeAssistant = request.app["hass"]
        entry = hass.config_entries.async_get_entry(entry_id)
        if entry is None or entry.domain != DOMAIN:
            return self.json_message("Entry not found", status_code=404)

        if not hasattr(entry, "runtime_data") or entry.runtime_data is None:
            return self.json_message("Integration initialising", status_code=503)

        data: RoombaData = entry.runtime_data

        date_str = request.query.get("date")
        if date_str:
            try:
                target_date = datetime.date.fromisoformat(date_str)
            except ValueError:
                return self.json_message(
                    f"Invalid date '{date_str}' — expected YYYY-MM-DD",
                    status_code=400,
                )
        else:
            target_date = dt_util.now().date()

        if data.mission_store is None:
            return self.json({
                "missions": 0, "area_m2": None, "stuck_events": 0,
                "demand_cleans": 0, "filter_hours_today": None,
                "battery_cycles_today": None,
            })

        # MAX_RECORDS (365) is the full retention window — a large enough
        # lookback to find both target_date's records AND the most recent
        # record strictly before it, however far back that is.
        from .mission_store import MAX_RECORDS
        all_records = data.mission_store.query(MAX_RECORDS + 30)

        day_records = [
            r for r in all_records
            if (lambda dt: dt is not None and dt_util.as_local(dt).date() == target_date)
            (dt_util.parse_datetime(r.get("started_at", "")))
        ]

        areas = [r["area_sqft"] for r in day_records if r.get("area_sqft") is not None]
        stuck_events = sum(
            1 for r in day_records
            if r.get("result") in ("stuck", "stuck_and_abandoned")
        )
        demand_cleans = sum(1 for r in day_records if r.get("initiator") == "demand")

        filter_hours_today = self._lifetime_delta_for_day(
            all_records, target_date, "bbrun_hr",
        )
        battery_cycles_today_raw = self._lifetime_delta_for_day(
            all_records, target_date, "battery_cycles",
        )

        return self.json({
            "missions": len(day_records),
            "area_m2": round(sum(areas) * SQFT_TO_M2, 1) if areas else None,
            "stuck_events": stuck_events,
            "demand_cleans": demand_cleans,
            "filter_hours_today": (
                round(filter_hours_today, 1)  # bbrun_hr is already in hours
                if filter_hours_today is not None else None
            ),
            "battery_cycles_today": (
                int(round(battery_cycles_today_raw))
                if battery_cycles_today_raw is not None else None
            ),
        })

    @staticmethod
    def _lifetime_delta_for_day(
        all_records: list[dict[str, Any]],
        target_date: datetime.date,
        field: str,
    ) -> float | None:
        """Return the TRUE increase of an absolute lifetime counter field
        attributable to target_date, or None if there's nothing to diff
        against.

        Both bbrun_hr and battery_cycles are monotonically-increasing
        snapshots of the robot's lifetime counters, captured at mission end
        (NOT per-mission deltas — see callbacks.py async_record_mission()).
        Because these counters only change DURING a mission, the increase
        over a calendar day equals (latest value among that day's records)
        minus (latest value among all records strictly before that day) —
        regardless of how many days back that prior record is, since the
        counter cannot have changed in the gap. all_records must already be
        sorted ascending by started_at (MissionStore.query()'s contract).
        """
        on_day: float | None = None
        before_day: float | None = None
        for r in all_records:
            dt = dt_util.parse_datetime(r.get("started_at", ""))
            if dt is None:
                continue
            r_date = dt_util.as_local(dt).date()
            value = r.get(field)
            if value is None:
                continue
            if r_date == target_date:
                on_day = float(value)   # query() is ascending — last write wins
            elif r_date < target_date:
                before_day = float(value)
        if on_day is None:
            return None
        if before_day is None:
            # No record at all before target_date — cannot isolate this
            # day's share of the counter (would silently understate or
            # overstate it). Honest "unknown" rather than assuming 0.
            return None
        return max(0.0, on_day - before_day)


class MissionMapJsonView(HomeAssistantView):
    """GET /api/roomba_plus/{entry_id}/missions/{record_id}/map.json

    v3.3.0 MISSION-MAP — coordinate-level coverage of ONE finished
    mission (the official app's "Mission Cleaning Map", but for every
    mission in the history). Raw data endpoint: the card renders
    client-side from this; map.png below is the server-rendered bonus.

    record_id path segment "latest" resolves to the most recent mission.
    Error mapping: 404 record unknown / no pmaps_info / empty coverage
    layer (untested lewis firmware), 409 verification-gate mismatch
    (never serve the wrong map), 502 cloud transport failure.
    """

    url = "/api/roomba_plus/{entry_id}/missions/{record_id}/map.json"
    name = "api:roomba_plus:mission_map_json"
    requires_auth = True

    async def get(
        self, request: web.Request, entry_id: str, record_id: str
    ) -> web.Response:
        payload, data, err = await _mission_map_payload(request, entry_id, record_id)
        if err is not None:
            return self.json_message(err[1], status_code=err[0])
        # Current room polygons as rendering context (names resolved) —
        # deliberately the CURRENT map version: room shapes are the
        # backdrop, the mission's own data is the coverage layer.
        rooms: dict[str, list] = {}
        aligner = data.umf_aligner
        if aligner is not None and aligner.aligned:
            name_of = aligner.rid_to_name()
            for rid, poly in aligner.room_polygons_umf.items():
                rooms[name_of.get(rid, rid)] = [list(p) for p in poly]
        return self.json({**payload, "rooms": rooms})


