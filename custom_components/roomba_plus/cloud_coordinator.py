    async def _fetch_active_umf(
        self, pmaps: list[dict]
    ) -> dict[str, Any] | None:
        """Fetch UMF floor plan for the active pmap.

        Returns a dict with keys:
          keepoutzones:   list of keep-out zone dicts (with centroid coords)
          observed_zones: list of robot-learned obstacle dicts (with centroids)
          points2d:       list of wall boundary points (for F8 UmfAligner, v2.3)
          pmap_id:        the pmap_id this UMF belongs to
          version_id:     the pmap version fetched

        Coordinate space: UMF units (Q6 open — store with space='umf' tag,
        do not use for pose-space rendering until Q6 confirmed in v2.3).

        Returns None when no active pmap or UMF fetch fails.
        """
        # Resolve active_pmap_id directly from the pmaps list passed in.
        # Do NOT use self.active_pmap_id here — that reads self.data which is
        # None during the first _async_update_data call (HA only sets self.data
        # after a successful return). Using self.data would always return None
        # on the first update, silently skipping the UMF fetch every startup.
        active_id: str | None = None
        for _p in pmaps:
            _pmapv = _p.get("active_pmapv_details", {}).get("active_pmapv", {})
            _pid = _pmapv.get("pmap_id") or _p.get("pmap_id")
            if _pid:
                active_id = _pid
                break

        if not active_id:
            _LOGGER.debug(
                "iRobot cloud: UMF fetch skipped for %s — "
                "could not determine active_pmap_id from pmaps list",
                self.blid,
            )
            return None

        for pmap in pmaps:
            # pmap structure variants for version_id resolution.
            # Only Variant B is confirmed from live debug logs (June 2026).
            # Variant A and C are unconfirmed but not disproven — kept as
            # defensive fallbacks to avoid breaking unknown robot/firmware types.
            #
            # Variant A (unconfirmed — original v2.3.0 assumption):
            #   version_id: active_pmapv_details.active_pmapv.active_pmapv_id
            #
            # Variant B (confirmed — veronoicc + Thonno, lewis 22.52.10, June 2026):
            #   version_id: active_pmapv_details.active_pmapv.last_user_pmapv_id
            #
            # Variant C (unconfirmed — inferred from ia74/roomba_rest980 CloudApi.py):
            #   version_id: pmap["active_pmapv_id"] (root level)
            #
            # All variants use active_pmapv_details.active_pmapv.pmap_id for pmap_id
            # match, except Variant C which falls back to pmap["pmap_id"] at root.
            #
            details = pmap.get("active_pmapv_details", {})
            pmapv   = details.get("active_pmapv", {})

            # pmap_id match — try Variant A/B first, then Variant C
            pmap_id_candidate = pmapv.get("pmap_id") or pmap.get("pmap_id")
            if pmap_id_candidate != active_id:
                continue

            # version_id — try all three variants in order
            version_id = (
                pmapv.get("active_pmapv_id")       # Variant A
                or pmapv.get("last_user_pmapv_id") # Variant B (lewis 22.52.10)
                or pmap.get("active_pmapv_id")     # Variant C (ia74 source)
            )
            if not version_id:
                _LOGGER.debug(
                    "iRobot cloud: UMF fetch skipped for %s — "
                    "could not resolve version_id (pmapv keys: %s)",
                    self.blid, list(pmapv.keys()),
                )
                return None
            try:
                umf_raw = await self.api.get_pmap_umf(
                    self.blid, active_id, version_id
                )
            except Exception as exc:  # noqa: BLE001
                _LOGGER.debug(
                    "iRobot cloud: UMF fetch failed for %s "
                    "(pmap_id=%s version_id=%s) — %s: %s",
                    self.blid, active_id, version_id,
                    type(exc).__name__, exc,
                )
                return None

            _LOGGER.debug(
                "iRobot cloud: UMF raw response keys=%s points2d=%d "
                "keepoutzones=%d observed_zones=%d for %s",
                list(umf_raw.keys()) if isinstance(umf_raw, dict) else type(umf_raw).__name__,
                len(umf_raw.get("points2d") or []) if isinstance(umf_raw, dict) else 0,
                len(umf_raw.get("keepoutzones") or []) if isinstance(umf_raw, dict) else 0,
                len(umf_raw.get("observed_zones") or []) if isinstance(umf_raw, dict) else 0,
                self.blid,
            )

            # UMF response structure confirmed from veronoicc debug log and
            # ia74/roomba_rest980 camera.py source (June 2026):
            # Container format with keys ['format_version', 'debug_file_header', 'maps']
            # where geometry (points2d, keepoutzones, observed_zones) is in maps[].
            # UMF response structure confirmed from veronoicc debug log and
            # ia74/roomba_rest980 camera.py source (June 2026):
            # Container format — geometry and region polygons are in maps[].
            # regions in maps[] contain geometry.ids referencing points2d,
            # unlike regions from get_pmaps() which only have metadata.
            maps = umf_raw.get("maps") or []
            geo: dict[str, Any] = {
                "points2d": [], "keepoutzones": [],
                "observed_zones": [], "regions": [],
                "region_suggestions": [],   # v3.2.0 ROOM-TYPE-SUGGEST
            }
            for m in maps:
                if not isinstance(m, dict):
                    continue
                geo["points2d"].extend(m.get("points2d") or [])
                geo["keepoutzones"].extend(m.get("keepoutzones") or [])
                geo["observed_zones"].extend(m.get("observed_zones") or [])
                # Regions from UMF maps contain geometry.ids — needed by UmfAligner
                # to resolve room polygons. Regions from get_pmaps() only have metadata.
                geo["regions"].extend(m.get("regions") or [])
                # v3.2.0 ROOM-TYPE-SUGGEST — iRobot's own ML room-type
                # classifier output, confirmed present in a real captured
                # pmap (PyRoomba r.json, July 2026 — see
                # MISSIONSTORE_FIELD_REGISTRY.md). Reliability across many
                # pmaps unconfirmed (only one sample seen) — used only as
                # a conservative fallback, never overriding a user-set
                # region name. See sensor.py's id_to_display_name().
                geo["region_suggestions"].extend(m.get("region_suggestions") or [])

            _LOGGER.debug(
                "iRobot cloud: UMF parsed — "
                "%d map(s) -> points2d=%d keepoutzones=%d observed_zones=%d regions=%d for %s",
                len(maps),
                len(geo["points2d"]),
                len(geo["keepoutzones"]),
                len(geo["observed_zones"]),
                len(geo["regions"]),
                self.blid,
            )

            return {
                "keepoutzones": [
                    {**z, "space": "umf"}
                    for z in geo["keepoutzones"]
                ],
                "observed_zones": [
                    {**z, "space": "umf"}
                    for z in geo["observed_zones"]
                ],
                "points2d":   geo["points2d"],
                "regions":    geo["regions"],
                "region_suggestions": geo["region_suggestions"],   # v3.2.0 ROOM-TYPE-SUGGEST
                "pmap_id":    active_id,
                "version_id": version_id,
            }
        return None
