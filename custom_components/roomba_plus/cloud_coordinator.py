    @property
    def active_pmap_id(self) -> str | None:
        """Return the pmap_id of the currently active Smart Map.

        v2.7.4 (PMAP-NEWEST): selects the pmap whose user_pmapv_id timestamp
        is most recent — that is the map the robot has been actively using and
        updating after each cleaning run.  The cloud API returns all maps for
        an account without a guaranteed ordering; for robots with multiple Smart
        Maps (e.g. two-storey homes or after a map retrain), returning the first
        entry silently picks the wrong map.

        Lexicographic comparison of YYMMDDTHHMMSS strings is sufficient since
        the format is fixed-width and zero-padded.

        Falls back to _seeded_pmap_id (set from local MQTT) when no cloud data
        is available (pre-first-fetch window).
        """
        if not self.data:
            return getattr(self, "_seeded_pmap_id", None)
        best_pid: str | None = None
        best_ts: str = ""
        for pmap in self.data.get("pmaps", []):
            details = pmap.get("active_pmapv_details", {})
            pmapv = details.get("active_pmapv", {})
            pid = pmapv.get("pmap_id")
            if not pid:
                continue
            ts: str = (
                pmapv.get("last_user_pmapv_id", "")   # Variant B — lewis 22.52.x
                or pmapv.get("active_pmapv_id", "")   # Variant A
                or pmap.get("active_pmapv_id", "")    # Variant C — root level (ia74 / older fw)
            )
            if ts > best_ts:
                best_pid = pid
                best_ts = ts
        return best_pid or getattr(self, "_seeded_pmap_id", None)

    @property
    def active_user_pmapv_id(self) -> str | None:
        """Return the user_pmapv_id (map version) for the active pmap.

        This is the version token the robot requires in start commands that
        target specific rooms (vacuum.clean_area, clean_room). Sourced from
        cloud data which is always authoritative — local MQTT pmaps may be
        stale for lewis firmware robots that do not broadcast pmaps updates.

        Tries three cloud API variants in order (same logic as UMF fetch):
          Variant A: active_pmapv_details.active_pmapv.active_pmapv_id
          Variant B: active_pmapv_details.active_pmapv.last_user_pmapv_id
                     (confirmed for lewis 22.52.10, veronoicc + Thonno)
          Variant C: pmap root-level active_pmapv_id (ia74 fallback)
        """
        if not self.data:
            return None
        active_id = self.active_pmap_id
        if not active_id:
            return None
        for pmap in self.data.get("pmaps", []):
            details = pmap.get("active_pmapv_details", {})
            pmapv = details.get("active_pmapv", {})
            pmap_id_candidate = pmapv.get("pmap_id") or pmap.get("pmap_id")
            if pmap_id_candidate != active_id:
                continue
            return (
                pmapv.get("active_pmapv_id")       # Variant A
                or pmapv.get("last_user_pmapv_id") # Variant B (lewis 22.52.10)
                or pmap.get("active_pmapv_id")     # Variant C
            ) or None
        return None

    @property
    def learning_percentage(self) -> int | None:
        """IA74-LP (v2.6.0): Return map learning_percentage from the active pmap.

        This is the SMART map's own completeness score (0–100). Exposed as a
        SensorDeviceClass.MEASUREMENT sensor. Gate: SMART + cloud only.
        Returns None when no data or the field is absent.
        """
        if not self.data:
            return None
        for pmap in self.data.get("pmaps", []):
            details = pmap.get("active_pmapv_details", {})
            pct = details.get("map_header", {}).get("learning_percentage")
            if pct is not None:
                try:
                    return int(pct)
                except (TypeError, ValueError):
                    pass
        return None

    def seed_pmap_id_from_local(self, reported_state: dict) -> None:
        """IA74-PMAP — Seed active_pmap_id from local MQTT pmaps field on startup.

        Called once in async_setup_entry after the robot connects, before the
        first cloud fetch.  Only seeds when coordinator data is None so it
        never overrides real cloud data.

        v2.7.5 (SEED-FIX): iterates all pmaps and picks the one with the
        newest pmapv timestamp, mirroring the cloud-side active_pmap_id logic.
        Previously took pmaps[0] unconditionally; on multi-map robots the list
        is ordered oldest-first, so the inactive map was seeded and used until
        the first cloud fetch completed.

        Args:
            reported_state: the reported section of the robot's MQTT state.
        """
        if self.data is not None:
            return   # cloud data already present — don't override
        pmaps: list[dict] = reported_state.get("pmaps", [])
        if not pmaps:
            return
        best_pid: str | None = None
        best_ts: str = ""
        for p in pmaps:
            if not isinstance(p, dict):
                continue
            pid = next(iter(p), None)
            ts  = p.get(pid, "") if pid else ""
            if pid and ts > best_ts:
                best_pid = pid
                best_ts  = ts
        if best_pid:
            self._seeded_pmap_id = best_pid
            _LOGGER.debug(
                "iRobot cloud: seeded active_pmap_id=%s from local MQTT "
                "(pmapv=%s, %d map(s)) for %s",
                best_pid, best_ts, len(pmaps), self.blid,
            )

    @property
    def regions(self) -> list[dict[str, Any]]:
        """Return regions from the ACTIVE pmap only.

        Deliberately ignores disabled/old maps to prevent room-name collisions
        when multiple Smart Maps exist in the same iRobot account. A user with
        an old disabled map and a current map that share room names would
        otherwise get silent wrong-map cleans (Case 1) or rooms_different_floors
        errors (Case 2) from _resolve_rooms.
        """
        if not self.data:
            return []
        active_id = self.active_pmap_id
        if not active_id:
            return []
        for pmap in self.data.get("pmaps", []):
            details = pmap.get("active_pmapv_details", {})
            pmapv = details.get("active_pmapv", {})
            if pmapv.get("pmap_id") != active_id:
                continue  # skip disabled / inactive maps
            regions_raw = details.get("regions", [])
            if not regions_raw:
                return []
            # The iRobot pmaps API uses "region_id" as the identifier key
            # in some account/firmware variants, and "id" in others.
            # Try both to be robust — confirmed as a real divergence from
            # live debug data (June 2026): first_region_keys determines which
            # is present. An empty id silently breaks CR4 attribute generation.
            def _rid(r: dict) -> str:
                return str(r.get("region_id") or r.get("id") or "")

            return [
                {
                    "id":             _rid(region),
                    "name":           region.get("name", ""),
                    "region_type":    region.get("region_type", "default"),
                    "pmap_id":        active_id,
                    "time_estimates": _parse_time_estimates(
                        region.get("time_estimates") or []
                    ),
                }
                for region in regions_raw
                if _rid(region)   # skip entries with no resolvable ID
            ]
        return []

    @property
    def zones(self) -> list[dict[str, Any]]:
        """Return custom zones from the ACTIVE pmap only.

        Same active-map filter as regions — prevents stale zone data from
        disabled maps contaminating zone_data lookups.
        """
        if not self.data:
            return []
        active_id = self.active_pmap_id
        if not active_id:
            return []
        for pmap in self.data.get("pmaps", []):
            details = pmap.get("active_pmapv_details", {})
            pmapv = details.get("active_pmapv", {})
            if pmapv.get("pmap_id") != active_id:
                continue
            zones_raw = details.get("zones", [])
            if not zones_raw:
                return []

            def _zid(z: dict) -> str:
                return str(z.get("zone_id") or z.get("id") or "")

            return [
                {
                    "id":        _zid(zone),
                    "name":      zone.get("name", ""),
                    "zone_type": zone.get("zone_type", "default"),
                    "pmap_id":   active_id,
                }
                for zone in zones_raw
                if _zid(zone)
            ]
        return []

