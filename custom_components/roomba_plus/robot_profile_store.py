"""RobotProfileStore — centralised learned state for Roomba+ self-calibration.

Persistent home for all machine-learned model state that accumulates across HA
restarts. Separates learned data from raw data stores and exposes a reset()
action so users can wipe learned patterns (e.g. after moving house) without
losing mission history.

Lifecycle:
  - Instantiated once in async_setup_entry.
  - Store in entry.runtime_data.robot_profile_store.
  - Call async_load() before reading any values.
  - Call async_save() after any write operation.
  - async_reset() wipes all learned fields and saves immediately.

Generated by aurora@aurora-smart-home (ha-integration-dev skill)
https://github.com/tonylofgren/aurora-smart-home
"""
from __future__ import annotations

import logging
import statistics
from dataclasses import dataclass, field
from typing import Any

from homeassistant.core import HomeAssistant
from homeassistant.helpers.storage import Store

_LOGGER = logging.getLogger(__name__)

STORAGE_KEY_PREFIX = "roomba_plus_robot_profile"
STORAGE_VERSION = 1

# EMA decay factor for per-room dirtiness index (L5).
# α = 0.2 gives a slow-decay signal: new missions have 20 % weight,
# history retains 80 %. Stable after ~10 missions per room.
_ROOM_DIRT_EMA_ALPHA = 0.2

# Minimum number of missions before coverage_baseline is established (L6).
_COVERAGE_BASELINE_MIN_MISSIONS = 20


@dataclass
class RobotProfileStore:
    """Centralised store for all self-calibrated learned robot state.

    Fields
    ------
    Maintenance (migrated from L2 in MaintenanceStore — mirrors are kept
    there for backward compat; this store is the canonical source for
    RobotProfileStore consumers):
        learned_filter_hours    — median filter replacement interval, hours
        learned_brush_hours     — median brush replacement interval, hours

    Dirt patterns (L1 data, mirrored from DirtThresholdManager):
        baseline_by_weekday     — {0-6: median dirt density} for weekday-aware baseline

    Per-room dirtiness index (L5):
        room_dirt_index         — {rid: EMA of passCount/m²} per room

    Mission statistics (L3 anomaly baseline):
        mission_duration_mean   — rolling mean of mission duration_min
        mission_duration_std    — rolling std of mission duration_min
        mission_area_mean       — rolling mean of area_sqft

    Coverage baseline (L6):
        coverage_baseline       — personal edge_coverage_ratio baseline
        coverage_mission_count  — number of missions used in baseline
    """

    # Maintenance (read-only mirrors — written by MaintenanceStore)
    learned_filter_hours: float | None = None
    learned_brush_hours: float | None = None

    # Dirt patterns (0=Mon … 6=Sun → median dirt density)
    baseline_by_weekday: dict[int, float] = field(default_factory=dict)

    # Per-room dirtiness index: rid → EMA(passCount / area_m2)
    room_dirt_index: dict[str, float] = field(default_factory=dict)

    # Mission statistics (for L3 anomaly baseline, consolidated here for L8)
    mission_duration_mean: float | None = None
    mission_duration_std: float | None = None
    mission_area_mean: float | None = None

    # Navigation efficiency baseline (L6)
    coverage_baseline: float | None = None
    coverage_mission_count: int = 0

    # ── Persistence ───────────────────────────────────────────────────────────

    async def async_load(self, hass: HomeAssistant, entry_id: str) -> None:
        """Load persisted learned state from hass.storage."""
        store = Store(hass, STORAGE_VERSION, f"{STORAGE_KEY_PREFIX}_{entry_id}")
        data: dict | None = await store.async_load()
        if not data:
            _LOGGER.debug("RobotProfileStore: no persisted data for %s", entry_id)
            return
        try:
            lf = data.get("learned_filter_hours")
            self.learned_filter_hours = float(lf) if lf is not None else None
            lb = data.get("learned_brush_hours")
            self.learned_brush_hours = float(lb) if lb is not None else None

            # Weekday baselines — keys stored as strings in JSON, convert to int
            raw_wdb = data.get("baseline_by_weekday", {})
            self.baseline_by_weekday = {int(k): float(v) for k, v in raw_wdb.items()}

            # Per-room dirt index
            raw_rdi = data.get("room_dirt_index", {})
            self.room_dirt_index = {str(k): float(v) for k, v in raw_rdi.items()}

            # Mission statistics
            dm = data.get("mission_duration_mean")
            self.mission_duration_mean = float(dm) if dm is not None else None
            ds = data.get("mission_duration_std")
            self.mission_duration_std = float(ds) if ds is not None else None
            am = data.get("mission_area_mean")
            self.mission_area_mean = float(am) if am is not None else None

            # Coverage baseline
            cb = data.get("coverage_baseline")
            self.coverage_baseline = float(cb) if cb is not None else None
            self.coverage_mission_count = int(data.get("coverage_mission_count", 0))

            _LOGGER.debug(
                "RobotProfileStore: loaded — rooms=%d coverage_baseline=%s",
                len(self.room_dirt_index),
                f"{self.coverage_baseline:.3f}" if self.coverage_baseline else "None",
            )
        except (TypeError, ValueError, KeyError) as exc:
            _LOGGER.warning("RobotProfileStore: failed to load data: %s", exc)

    async def async_save(self, hass: HomeAssistant, entry_id: str) -> None:
        """Persist all learned state to hass.storage."""
        store = Store(hass, STORAGE_VERSION, f"{STORAGE_KEY_PREFIX}_{entry_id}")
        await store.async_save({
            "learned_filter_hours": self.learned_filter_hours,
            "learned_brush_hours": self.learned_brush_hours,
            "baseline_by_weekday": {str(k): v for k, v in self.baseline_by_weekday.items()},
            "room_dirt_index": self.room_dirt_index,
            "mission_duration_mean": self.mission_duration_mean,
            "mission_duration_std": self.mission_duration_std,
            "mission_area_mean": self.mission_area_mean,
            "coverage_baseline": self.coverage_baseline,
            "coverage_mission_count": self.coverage_mission_count,
        })

    async def async_reset(self, hass: HomeAssistant, entry_id: str) -> None:
        """Wipe all learned state and persist immediately.

        Called when the user invokes the reset_robot_profile service — e.g.
        after moving house, getting a new robot, or replacing the battery pack.
        Mission history (MissionStore) is unaffected.
        """
        self.learned_filter_hours = None
        self.learned_brush_hours = None
        self.baseline_by_weekday = {}
        self.room_dirt_index = {}
        self.mission_duration_mean = None
        self.mission_duration_std = None
        self.mission_area_mean = None
        self.coverage_baseline = None
        self.coverage_mission_count = 0
        await self.async_save(hass, entry_id)
        _LOGGER.info("RobotProfileStore: reset for entry %s", entry_id)

    # ── Public update API ─────────────────────────────────────────────────────

    def update_from_maintenance_store(
        self,
        learned_filter: float | None,
        learned_brush: float | None,
    ) -> None:
        """Mirror learned maintenance intervals from MaintenanceStore.

        Called after each reset_filter / reset_brush event so this store
        reflects the canonical values for diagnostics and L8 health score.
        """
        self.learned_filter_hours = learned_filter
        self.learned_brush_hours = learned_brush

    def update_room_dirt_index(
        self,
        rid: str,
        pass_count: int,
        area_m2: float,
    ) -> None:
        """Update per-room dirtiness EMA from a completed pass.

        L5 — called after each mission for every room event with status=0 or 6.
        Skips rooms where area_m2 is zero to avoid division errors.

        Args:
            rid: iRobot region ID string.
            pass_count: number of passes the robot made in this room this mission.
            area_m2: room area in square metres from UMF or local accumulation.
        """
        if area_m2 <= 0:
            return
        current_density = pass_count / area_m2
        if rid in self.room_dirt_index:
            # EMA update: new = α × current + (1-α) × old
            old = self.room_dirt_index[rid]
            self.room_dirt_index[rid] = (
                _ROOM_DIRT_EMA_ALPHA * current_density
                + (1 - _ROOM_DIRT_EMA_ALPHA) * old
            )
        else:
            # First observation — seed with current value
            self.room_dirt_index[rid] = current_density
        _LOGGER.debug(
            "RobotProfileStore: room %s dirt index → %.3f",
            rid, self.room_dirt_index[rid],
        )

    def update_coverage_baseline(self, ratio: float) -> None:
        """Update navigation efficiency baseline with a new mission ratio.

        L6 — called after each mission when edge_coverage_ratio is available.
        Baseline is established after _COVERAGE_BASELINE_MIN_MISSIONS observations
        using a simple running mean (stable signal; EMA would over-weight outliers
        in the initial window before convergence).
        """
        self.coverage_mission_count += 1
        if self.coverage_baseline is None:
            self.coverage_baseline = ratio
        else:
            # Running mean: new_mean = old_mean + (x - old_mean) / n
            n = self.coverage_mission_count
            self.coverage_baseline = self.coverage_baseline + (ratio - self.coverage_baseline) / n
        _LOGGER.debug(
            "RobotProfileStore: coverage baseline → %.3f (n=%d)",
            self.coverage_baseline, self.coverage_mission_count,
        )

    @property
    def coverage_baseline_ready(self) -> bool:
        """True when enough missions have been observed for a stable baseline."""
        return (
            self.coverage_baseline is not None
            and self.coverage_mission_count >= _COVERAGE_BASELINE_MIN_MISSIONS
        )

    def update_mission_stats(
        self,
        records: list[dict[str, Any]],
    ) -> None:
        """Recompute mission duration/area statistics from recent records.

        L3/L8 — called after each mission from the callback chain.
        Uses the last 30 days of records passed in by the caller (avoids
        direct MissionStore dependency — cleaner separation of concerns).

        Args:
            records: list of MissionStore record dicts from query(days=30).
        """
        durations = [
            r["duration_min"]
            for r in records
            if isinstance(r.get("duration_min"), int) and r["duration_min"] > 0
        ]
        areas = [
            r["area_sqft"]
            for r in records
            if isinstance(r.get("area_sqft"), (int, float)) and r["area_sqft"] > 0
        ]
        if len(durations) >= 5:
            self.mission_duration_mean = statistics.mean(durations)
            self.mission_duration_std = (
                statistics.stdev(durations) if len(durations) >= 2 else 0.0
            )
        if len(areas) >= 5:
            self.mission_area_mean = statistics.mean(areas)

    def room_dirt_relative(self) -> dict[str, float]:
        """Return per-room dirtiness relative to the household average.

        L5 consumer helper — 1.0 = average; > 1.5 = notably dirty room.
        Returns empty dict when fewer than 2 rooms have been indexed.
        """
        if len(self.room_dirt_index) < 2:
            return {}
        avg = statistics.mean(self.room_dirt_index.values())
        if avg <= 0:
            return {}
        return {rid: val / avg for rid, val in self.room_dirt_index.items()}

    # ── L8 (v2.7.0) — Composite robot health score ────────────────────────────

    def compute_health_score(
        self,
        battery_retention_pct: float | None,
        nav_efficiency_ratio: float | None,
        cleaning_speed_trend: str | None,
        consecutive_anomalous: int,
        stuck_rate_30d: float | None,
    ) -> float | None:
        """L8 — weighted composite health score 0–100.

        Weights:
          Battery retention    25%  — measures physical degradation
          Nav efficiency       20%  — measures spatial coverage quality
          Cleaning speed trend 20%  — measures throughput trend
          Anomaly rate         20%  — measures mission consistency
          Stuck rate           15%  — measures obstacle avoidance

        Returns None when fewer than 3 signals are available (insufficient data
        to form a meaningful score).  Each missing signal is skipped and the
        remaining weights are renormalised.
        """
        components: list[tuple[float, float]] = []   # (score, weight)

        # 1. Battery retention (25%): 50% retention → 0 pts, 100% → 100 pts
        if battery_retention_pct is not None:
            s = max(0.0, min(100.0, (float(battery_retention_pct) - 50.0) * 2.0))
            components.append((s, 0.25))

        # 2. Navigation efficiency (20%): current/baseline ratio
        #    ratio ≥ 1.0 → 100; ratio ≤ 0.5 → 0 (linear between)
        if self.coverage_baseline_ready and nav_efficiency_ratio is not None:
            s = max(0.0, min(100.0, (float(nav_efficiency_ratio) - 0.5) * 200.0))
            components.append((s, 0.20))

        # 3. Cleaning speed trend (20%)
        _trend_pts = {"improving": 100.0, "stable": 80.0, "declining": 40.0}
        if cleaning_speed_trend in _trend_pts:
            components.append((_trend_pts[cleaning_speed_trend], 0.20))

        # 4. Mission anomaly rate (20%): consecutive anomalous missions
        #    0 → 100, 1 → 70, 2 → 40, 3+ → 10
        _anomaly_pts = {0: 100.0, 1: 70.0, 2: 40.0}
        anomaly_score = _anomaly_pts.get(consecutive_anomalous, 10.0)
        components.append((anomaly_score, 0.20))  # always available

        # 5. Stuck rate (15%): rate < 5% → 100; rate ≥ 30% → 0 (linear)
        if stuck_rate_30d is not None:
            s = max(0.0, min(100.0, (0.30 - float(stuck_rate_30d)) / 0.25 * 100.0))
            components.append((s, 0.15))

        if len(components) < 3:
            return None

        total_weight = sum(w for _, w in components)
        if total_weight == 0:
            return None

        score = sum(s * w for s, w in components) / total_weight
        return round(min(100.0, max(0.0, score)), 1)
