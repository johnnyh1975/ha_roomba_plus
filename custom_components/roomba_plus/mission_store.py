    def compute_rolling_stats(self, days: int = 30) -> dict | None:
        """Compute rolling mean/std for duration_min and area_sqft.

        L3 — Returns None when fewer than 20 missions are available (insufficient
        history to establish a stable baseline for anomaly detection).

        Returns a dict with keys:
          duration_mean, duration_std,
          area_mean, area_std,
          recharge_mean,
          dirt_p75            (75th-percentile dirt_events from cloud-merged records)
        """
        records = self.query(days)
        if len(records) < 20:
            return None

        durations = [r["duration_min"] for r in records if r.get("duration_min") is not None]
        areas = [r["area_sqft"] for r in records
                 if r.get("area_sqft") is not None and r["area_sqft"] > 0]
        recharges = [r["recharge_min"] for r in records
                     if r.get("recharge_min") is not None]
        dirts = [r["dirt"] for r in records if r.get("dirt") is not None]

        if len(durations) < 20:
            return None

        dur_mean = statistics.mean(durations)
        dur_std  = statistics.stdev(durations) if len(durations) > 1 else 0.0
        area_mean = statistics.mean(areas) if areas else None
        area_std  = statistics.stdev(areas) if len(areas) > 1 else 0.0
        rch_mean  = statistics.mean(recharges) if recharges else 0.0

        # 75th-percentile dirt events
        dirt_p75: float | None = None
        if dirts:
            dirts.sort()
            dirt_p75 = dirts[int(len(dirts) * 0.75)]

        return {
            "duration_mean": dur_mean,
            "duration_std":  dur_std,
            "area_mean":     area_mean,
            "area_std":      area_std,
            "recharge_mean": rch_mean,
            "dirt_p75":      dirt_p75,
        }

    @staticmethod
    def compute_archive_stats(derived_records: list[dict]) -> dict | None:
        """Compute anomaly-detection baseline from ARC1 archive derived records.

        L3-ARC (v2.8.0) — identical contract to compute_rolling_stats() but
        consumes MissionArchive.all_derived_oldest_first() rather than local
        MissionStore records.  Used as a fallback baseline when the local store
        has < 20 missions.

        Field mapping from archive derived → stats dict:
          duration_min  → duration stats        (same field name)
          sqft          → area stats            (archive uses sqft, store uses area_sqft)
          dirt          → dirt percentile       (same field name)
          recharge_mean → 0.0                  (recharge_count not in minutes; skipped)

        Returns None when fewer than 20 completed-mission records are available.
        """
        completed = [
            r for r in derived_records
            if r.get("result") in ("completed", "cancelled_by_user")
        ]
        if len(completed) < 20:
            return None

        durations = []
        for r in completed:
            if r.get("duration_min") is None:
                continue
            try:
                durations.append(float(r["duration_min"]))
            except (TypeError, ValueError):
                continue
        areas = []
        for r in completed:
            if r.get("sqft") is None:
                continue
            try:
                _sqft = float(r["sqft"])
            except (TypeError, ValueError):
                continue
            if _sqft > 0:
                areas.append(_sqft)
        dirts = []
        for r in completed:
            if r.get("dirt") is None:
                continue
            try:
                dirts.append(float(r["dirt"]))
            except (TypeError, ValueError):
                continue

        if len(durations) < 20:
            return None

        dur_mean = statistics.mean(durations)
        dur_std  = statistics.stdev(durations) if len(durations) > 1 else 0.0
        area_mean = statistics.mean(areas) if areas else None
        area_std  = statistics.stdev(areas) if len(areas) > 1 else 0.0

        dirt_p75: float | None = None
        if dirts:
            dirts_sorted = sorted(dirts)
            dirt_p75 = dirts_sorted[int(len(dirts_sorted) * 0.75)]

        return {
            "duration_mean": dur_mean,
            "duration_std":  dur_std,
            "area_mean":     area_mean,
            "area_std":      area_std,
            "recharge_mean": 0.0,   # not available in ARC1 derived records
            "dirt_p75":      dirt_p75,
        }

    def anomaly_reason(
        self,
        record: dict,
        stats: dict,
        profile: Any = None,
    ) -> str | None:
        """v3.2.0 ANOMALY-EXPLAIN — same four conditions as is_anomalous()
        (which now delegates here), but returns WHICH one triggered instead
        of a bare bool. Checked in this fixed priority order when more than
        one would technically apply (rare in practice, since each is an
        early return on the same record — a mission that's both slow+low-
        coverage AND has an extreme dirt spike is an unusual combination):

          1. "obstacle_or_blockage" — slow AND low coverage
          2. "excessive_recharge"   — unusually long mid-mission recharge
          3. "dirt_spike"           — extreme dirt event
          4. "incomplete_coverage"  — pre-20-mission fallback vs robot profile

        Returns None when the record isn't anomalous by any of these.

        Deliberately does NOT cover robot_lifted or error_code — those are
        independent facts about a mission (see async_handle_explain_mission
        in services.py), not additional anomaly_reason categories. A
        mission can be robot_lifted and/or have an error_code regardless
        of whether any of the four reasons above also applies, so folding
        them into this priority chain would misrepresent them as
        competing explanations rather than orthogonal ones.
        """
        dur   = record.get("duration_min")
        area  = record.get("area_sqft")
        rch   = record.get("recharge_min")
        dirt  = record.get("dirt")

        # 1 — struggling: slow AND low coverage
        if (
            dur is not None
            and stats["duration_std"] > 0
            and dur > stats["duration_mean"] + 2 * stats["duration_std"]
            and area is not None
            and stats["area_mean"] is not None
            and stats["area_std"] > 0
            and area < stats["area_mean"] - stats["area_std"]
        ):
            return "obstacle_or_blockage"

        # 2 — excessive recharge
        if rch is not None and rch > stats["recharge_mean"] + 120:
            return "excessive_recharge"

        # 3 — extreme dirt spike
        if (
            dirt is not None
            and stats["dirt_p75"] is not None
            and stats["dirt_p75"] > 0
            and dirt > stats["dirt_p75"] * 2.5
        ):
            return "dirt_spike"

        # 4 — pre-20-mission fallback via robot profile. Only engaged when
        # no area statistics are available (stats["area_mean"] is None),
        # which happens when either fewer than 20 missions exist in the
        # window, or no mission in the window has area_sqft data
        # (600-series). With a full statistical baseline, this check is
        # intentionally skipped to avoid flagging legitimate partial room
        # cleans (e.g. single-room scheduled clean) as anomalous.
        if profile is not None and area is not None and stats["area_mean"] is None:
            typical = getattr(profile, "typical_coverage_sqft", None)
            if typical is not None and typical > 0:
                typical_m2 = typical * SQFT_TO_M2
                if area * SQFT_TO_M2 < typical_m2 * 0.4:
                    return "incomplete_coverage"

        return None

    def is_anomalous(
        self,
        record: dict,
        stats: dict,
        profile: Any = None,
    ) -> bool:
        """Return True when a record is statistically anomalous.

        L3 conditions (any one triggers) — see anomaly_reason() for the
        full detail and the specific reason each one maps to; this is now
        a thin wrapper so the two never drift out of sync with each other.
        """
        return self.anomaly_reason(record, stats, profile) is not None

    @property
    def consecutive_anomalous(self) -> int:
        """Count of consecutive most-recent missions that are anomalous.

        L3 — evaluated lazily when accessed; always reads fresh from _records.
        Falls back to archive_baseline (L3-ARC v2.8.0) when local stats are
        unavailable (< 20 local missions).
        Returns 0 when neither local stats nor archive baseline are available.
        """
        stats = self.compute_rolling_stats()
        if stats is None:
            stats = self.archive_baseline   # L3-ARC fallback
        if stats is None:
            return 0
        count = 0
        for record in reversed(self._records):
            if self.is_anomalous(record, stats):
                count += 1
            else:
                break
        return count

    def backfill_from_cloud(
        self,
        cloud_records: list[dict],
        tolerance_sec: int = 120,
    ) -> BackfillResult:
        """Correct local MissionStore records using cloud /missionhistory timestamps.

        The 980/900-series firmware resets mssnStrtTm to 0 in the mission-end
        MQTT message. When callbacks.py cannot capture a valid start timestamp,
        started_at falls back to wall-clock at recording time, making
        duration_min ≈ 0 and the record id incorrect.

        Matches each local record against cloud records by end timestamp
        (±tolerance_sec), then corrects:
          - started_at  — from cloud startTime
          - ended_at    — from cloud timestamp
          - duration_min — recomputed from corrected timestamps
          - id          — regenerated from corrected started_at
          - area_sqft   — from cloud sqft when local value is None

        Analytics fields (dirt, chrgM, wlBars, etc.) are merged into every
        matched record regardless of whether a timestamp correction is needed.

        Matching uses ended_at (local) vs timestamp (cloud) because ended_at
        is always reliably recorded at wall-clock time.

        Args:
            cloud_records: raw cloud records, newest-first from API.
            tolerance_sec: max seconds between local ended_at and cloud
                           timestamp to count as the same mission. Default 120 s
                           covers MQTT delivery lag and clock skew.

        Returns:
            BackfillResult(corrected, enriched) — counts of timestamp
            corrections and analytics field merges respectively.
        """
        if not cloud_records or not self._records:
            return BackfillResult(corrected=0, enriched=0)

        # Build index of cloud records keyed by end timestamp for O(1) lookup.
        cloud_by_end: dict[int, dict] = {}
        for cr in cloud_records:
            ts = cr.get("timestamp")
            if ts is not None:
                cloud_by_end.setdefault(int(ts), cr)

        corrected = 0
        enriched = 0
        for local in self._records:
            ended_str = local.get("ended_at", "")
            if not ended_str:
                continue
            try:
                ended_dt = datetime.fromisoformat(ended_str)
            except (ValueError, TypeError):
                continue
            if ended_dt.tzinfo is None:
                ended_dt = ended_dt.replace(tzinfo=timezone.utc)
            local_end_ts = int(ended_dt.timestamp())

            # Find nearest cloud record within tolerance
            best_cr: dict | None = None
            best_delta = tolerance_sec + 1
            for cloud_ts, cr in cloud_by_end.items():
                delta = abs(cloud_ts - local_end_ts)
                if delta <= tolerance_sec and delta < best_delta:
                    best_delta = delta
                    best_cr = cr

            if best_cr is None:
                continue

            cloud_start = best_cr.get("startTime")
            cloud_end   = best_cr.get("timestamp")
            if not cloud_start or not cloud_end:
                continue

            new_started = datetime.fromtimestamp(
                int(cloud_start), tz=timezone.utc
            )
            new_ended = datetime.fromtimestamp(
                int(cloud_end), tz=timezone.utc
            )

            # Only correct if timestamps differ meaningfully (>5 min)
            old_started_str = local.get("started_at", "")
            try:
                old_started = datetime.fromisoformat(old_started_str)
                if old_started.tzinfo is None:
                    old_started = old_started.replace(tzinfo=timezone.utc)
                delta_start = abs((new_started - old_started).total_seconds())
            except (ValueError, TypeError):
                delta_start = float("inf")

            # Always merge analytics fields regardless of timestamp delta.
            merge_wrote = self._merge_cloud_fields(local, best_cr)

            # area_sqft is the canonical local name; "sqft" above is the
            # raw cloud field, just merged (or already present locally).
            # This MUST run independently of the timestamp-correction
            # branch below: area_sqft has nothing to do with whether
            # started_at/ended_at needed correcting. Previously this lived
            # after the `delta_start < 300: continue`, so it only ever
            # fired on the rare mission that ALSO had bad timestamps —
            # leaving area_sqft permanently None on every mission whose
            # local timestamps were already accurate (confirmed in the
            # field: 24/24 local records, area_sqft never once populated
            # despite "sqft" correctly carrying real cloud values).
            area_wrote = self._backfill_area_sqft(local)

            if merge_wrote or area_wrote:
                enriched += 1

            # Only correct timestamps when they differ meaningfully (> 5 min).
            if delta_start < 300:
                continue

            new_duration = max(0, round(
                (new_ended - new_started).total_seconds() / 60
            ))
            new_id = f"m_{int(new_started.timestamp())}"

            local["started_at"]   = new_started.isoformat()
            local["ended_at"]     = new_ended.isoformat()
            local["duration_min"] = new_duration
            local["id"]           = new_id

            corrected += 1
            _LOGGER.debug(
                "MissionStore backfill: corrected %s → started_at=%s "
                "duration=%dmin (was %s, delta=%.0fs)",
                new_id, new_started.isoformat(),
                new_duration, old_started_str, delta_start,
            )

        if corrected or enriched:
            _LOGGER.info(
                "MissionStore backfill: corrected=%d timestamp(s), enriched=%d record(s)",
                corrected, enriched,
            )
        return BackfillResult(corrected=corrected, enriched=enriched)

    def merge_latest_from_cloud(
        self,
        cloud_records: list[dict],
        tolerance_sec: int = 120,
    ) -> bool:
        """Merge cloud analytics fields into the most recent local record.

        Called after the post-mission cloud refresh (F4b). Targets only
        the last record — the mission that just ended is newest in both stores.
        Uses the same ±tolerance_sec end-timestamp matching as backfill_from_cloud.

        Returns True if any field was written to the record.
        """
        if not self._records or not cloud_records:
            return False

        local = self._records[-1]
        ended_str = local.get("ended_at", "")
        if not ended_str:
            return False

        try:
            ended_dt = datetime.fromisoformat(ended_str)
        except (ValueError, TypeError):
            return False

        if ended_dt.tzinfo is None:
            ended_dt = ended_dt.replace(tzinfo=timezone.utc)

        local_end_ts = int(ended_dt.timestamp())

        best_cr: dict | None = None
        best_delta = tolerance_sec + 1
        for cr in cloud_records:
            ts = cr.get("timestamp")
            if ts is None:
                continue
            delta = abs(int(ts) - local_end_ts)
            if delta <= tolerance_sec and delta < best_delta:
                best_delta = delta
                best_cr = cr

        if best_cr is None:
            return False

        merge_wrote = self._merge_cloud_fields(local, best_cr)
        area_wrote = self._backfill_area_sqft(local)
        return merge_wrote or area_wrote

    async def async_backfill_statistics(
        self,
        hass: HomeAssistant,
        entry_id: str,
        robot_name: str,
    ) -> None:
        """Inject historical mission data into HA Long-Term Statistics.

        F7j -- backfills three external statistic series from MissionStore records,
        making a full year of history immediately available in the HA Statistics
        graph card.  async_add_external_statistics() is idempotent -- duplicate
        start timestamps are silently ignored, so this is safe to call on every
        startup and after every new record.

        Series:
          roomba_plus:{entry_id}_area_cleaned_m2    -- daily sum (m², has_sum)
          roomba_plus:{entry_id}_mission_duration_min -- daily sum (min, has_sum)
          roomba_plus:{entry_id}_missions_completed  -- daily count (missions, has_sum)

        Gate: area series requires cloud records with area_sqft.  Duration and
        completion count work for all robots from local records.
        """
        try:
            from homeassistant.components.recorder.statistics import (
                async_add_external_statistics,
            )
            from homeassistant.components.recorder.models import (
                StatisticData,
                StatisticMetaData,
            )
            # mean_type required from HA 2026.11 — pass as integer (0 = NONE)
            # to avoid import dependency on StatisticsMeanType which may not
            # exist in older HA versions. StatisticMetaData is a TypedDict so
            # it accepts any value without type checking.
            _mean_type_none = 0
        except ImportError:
            _LOGGER.debug(
                "MissionStore: recorder not available -- skipping statistics backfill"
            )
            return

        import inspect as _inspect

        async def _import_stats(meta, stats):
            """Call async_add_external_statistics, handling both sync and async variants."""
            result = async_add_external_statistics(hass, meta, stats)
            if _inspect.isawaitable(result):
                await result

        def _make_meta(name, statistic_id, unit):
            return StatisticMetaData(
                has_mean=False,
                has_sum=True,
                mean_type=_mean_type_none,  # 0 = NONE (no mean), required from HA 2026.11
                name=name,
                source=source,
                statistic_id=statistic_id,
                unit_class=None,            # required from HA 2025.11 — None = no unit conversion
                unit_of_measurement=unit,
            )

        if not self._records:
            return

        # Group records by hour-truncated start timestamp (LTS granularity)
        area_stats: list[StatisticData] = []
        duration_stats: list[StatisticData] = []
        completion_stats: list[StatisticData] = []

        for r in self._records:
            started_str = r.get("started_at", "")
            if not started_str:
                continue
            try:
                started_dt = dt_util.parse_datetime(started_str)
                if started_dt is None:
                    continue
                if started_dt.tzinfo is None:
                    started_dt = started_dt.replace(tzinfo=timezone.utc)
                # Truncate to hour for LTS
                bucket = started_dt.replace(minute=0, second=0, microsecond=0)
            except (ValueError, TypeError):
                continue

            area_sqft = r.get("area_sqft")
            duration_min = r.get("duration_min") or 0
            is_completed = r.get("result") in ("completed", "stuck_and_resumed")

            if area_sqft is not None:
                area_m2 = round(float(area_sqft) * SQFT_TO_M2, 2)
                area_stats.append(StatisticData(start=bucket, sum=area_m2))

            duration_stats.append(StatisticData(start=bucket, sum=float(duration_min)))
            completion_stats.append(StatisticData(start=bucket, sum=1.0 if is_completed else 0.0))

        source = "roomba_plus"

        # HA validates statistic_id strictly: domain:object_id where object_id
        # must be lowercase letters, digits and underscores only.
        # Entry IDs are ULIDs (uppercase) — convert to lowercase.
        safe_entry_id = entry_id.lower()

        if area_stats:
            await _import_stats(
                _make_meta(f"{robot_name} \u2014 Area cleaned", f"{source}:{safe_entry_id}_area_cleaned_m2", "m\u00b2"),
                area_stats,
            )

        if duration_stats:
            await _import_stats(
                _make_meta(f"{robot_name} \u2014 Mission duration", f"{source}:{safe_entry_id}_mission_duration_min", "min"),
                duration_stats,
            )

        if completion_stats:
            await _import_stats(
                _make_meta(f"{robot_name} \u2014 Missions completed", f"{source}:{safe_entry_id}_missions_completed", "missions"),
                completion_stats,
            )

        _LOGGER.debug(
            "MissionStore: statistics backfill complete for %s "
            "(%d area, %d duration, %d completion points)",
            entry_id,
            len(area_stats),
            len(duration_stats),
            len(completion_stats),
        )
