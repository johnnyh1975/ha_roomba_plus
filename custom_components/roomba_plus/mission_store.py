class MissionStore:
    """Append-only mission log — max 365 records FIFO.

    Lifecycle (must be respected by callers):
      - Instantiate in async_setup_entry.
      - Call async_load() immediately after instantiation.
      - Store in entry.runtime_data.mission_store.
      - No active listeners — no cancel/unload cleanup required.
    """

    def __init__(self) -> None:
        self._records: list[dict[str, Any]] = []
        # v2.8.0 L3-ARC — archive-computed stats dict injected at startup.
        # Provides a stable baseline for consecutive_anomalous when local
        # records are insufficient (< 20 missions).  Not persisted — computed
        # fresh from MissionArchive each time async_setup_entry runs.
        self.archive_baseline: dict | None = None
        # v3.3.0 STORE-ENCAP — lazy full-history id index for
        # append_validated(). None until first use; kept in sync by all
        # append paths afterwards. Not persisted (rebuilt on demand).
        self._record_ids: set[str] | None = None

    # ── Public read API (v3.3.0 STORE-ENCAP) ────────────────────────────────

    @property
    def records(self) -> Sequence[dict[str, Any]]:
        """Read-only view of all mission records, oldest first.

        v3.3.0 STORE-ENCAP — the canonical cross-module read access.
        Do NOT mutate the returned sequence or its dicts; use
        async_append (MQTT path) or append_validated (import path)
        so the MAX_RECORDS/dedup invariants stay in one place.
        Zero-copy by design (hot path in sensor filter_fns); the
        encapsulation guard test enforces the read-only contract.
        """
        return self._records

    # ── Persistence ───────────────────────────────────────────────────────────

    async def async_load(self, hass: HomeAssistant, entry_id: str) -> None:
        """Load persisted records from hass.storage."""
        store = Store(hass, STORAGE_VERSION, f"{STORAGE_KEY_PREFIX}_{entry_id}")
        data: dict | None = await store.async_load()
        if not data:
            _LOGGER.debug("MissionStore: no persisted data for %s", entry_id)
            return
        try:
            self._records = list(data.get("records", []))
            _LOGGER.debug("MissionStore: loaded %d record(s) for %s", len(self._records), entry_id)
        except (TypeError, ValueError) as exc:
            _LOGGER.warning("MissionStore: failed to load — %s; starting empty", exc)
            self._records = []

    async def async_save(self, hass: HomeAssistant, entry_id: str) -> None:
        """Persist current records to hass.storage."""
        store = Store(hass, STORAGE_VERSION, f"{STORAGE_KEY_PREFIX}_{entry_id}")
        await store.async_save({"records": self._records})

    # ── Write ─────────────────────────────────────────────────────────────────

    async def async_append(self, record: dict[str, Any]) -> None:
        """Append a record and trim to MAX_RECORDS FIFO.

        F4c — duplicate guard: drops records whose id matches a recent record
        with the same ended_at (= true MQTT re-delivery of the same event).

        v2.6.3 B3 — multi-recharge missions: the 980 firmware keeps the original
        mssnStrtTm across all charging segments, so Segment 2 and Segment 3 of
        a 3-segment mission would share the same record id as Segment 1 and be
        silently dropped by the original guard.  The fix: only drop when BOTH
        id AND ended_at match (true duplicate).  When only the id matches (same
        start_ts, different end = subsequent segment), assign a unique id by
        appending "_r<N>" before storing.
        """
        record_id = record.get("id")
        if record_id:
            for existing in self._records[-5:]:
                if existing.get("id") == record_id:
                    if existing.get("ended_at") == record.get("ended_at"):
                        # True MQTT re-delivery — same start AND same end
                        _LOGGER.debug(
                            "MissionStore: duplicate record %s dropped (MQTT re-delivery)",
                            record_id,
                        )
                        return
                    # Same start_ts, different end = subsequent recharge segment.
                    # Give it a unique id so it is stored alongside segment 1.
                    seg_n = sum(
                        1 for r in self._records
                        if r.get("id", "").startswith(record_id)
                    )
                    record = {**record, "id": f"{record_id}_r{seg_n}"}
                    _LOGGER.debug(
                        "MissionStore: multi-recharge segment stored as %s",
                        record["id"],
                    )
                    break
        self._records.append(record)
        if self._record_ids is not None and record.get("id"):
            self._record_ids.add(record["id"])
        self._trim()

    def append_validated(self, record: dict[str, Any]) -> bool:
        """Append with FULL-history id dedup + MAX_RECORDS trim.

        v3.3.0 STORE-ENCAP — the import path (REST mission-history
        import). Differs from async_append (the MQTT path) on purpose:
        - dedup checks the ENTIRE history, not a 5-record window
          (imports may contain records older than the window)
        - no recharge-segment "_r<N>" id splitting (imported records
          are final, not live MQTT segments)

        Returns False (and stores nothing) when the record has no
        string id or the id already exists. Does NOT save — callers
        batch a single async_save after the import loop.
        """
        record_id = record.get("id")
        if not record_id or not isinstance(record_id, str):
            return False
        if self._record_ids is None:
            self._record_ids = {
                r["id"] for r in self._records
                if isinstance(r.get("id"), str)
            }
        if record_id in self._record_ids:
            return False
        self._records.append(record)
        self._record_ids.add(record_id)
        self._trim()
        return True

    def _trim(self) -> None:
        """Enforce the MAX_RECORDS FIFO invariant — single home for it
        (v3.3.0 STORE-ENCAP; previously rebuilt ad hoc by the import
        endpoint, the documented bug source)."""
        if len(self._records) > MAX_RECORDS:
            self._records = self._records[-MAX_RECORDS:]
            # Ids may have been trimmed away — invalidate, rebuilt lazily.
            self._record_ids = None

    # ── Read ──────────────────────────────────────────────────────────────────

    def latest(self) -> dict[str, Any] | None:
        """Return the most recent record, or None."""
        return self._records[-1] if self._records else None

    def find_by_id(self, mission_id: str) -> dict[str, Any] | None:
        """v3.2.0 ANOMALY-EXPLAIN — look up a specific record by its
        `id` field (the "m_{unix_start_ts}" format used throughout this
        store) OR its `missionId` field (the cloud ULID, present on
        records enriched via backfill_from_cloud() — see
        _CLOUD_MERGE_SCALAR). v3.3.1 fix: previously only checked `id`,
        contradicting the documented contract ("the record's id field
        ... or the cloud missionId ULID — both resolve") — missionId
        never actually matched anything. Linear scan — record counts
        are bounded (MAX_RECORDS), so this is not a hot path worth
        indexing."""
        for record in self._records:
            if record.get("id") == mission_id or record.get("missionId") == mission_id:
                return record
        return None

