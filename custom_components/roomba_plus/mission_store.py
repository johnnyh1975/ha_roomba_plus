    def explain_mission(
        self,
        mission_id: str | None = None,
        record_override: dict[str, Any] | None = None,
    ) -> dict[str, Any] | None:
        """v3.2.0 ANOMALY-EXPLAIN — shared explanation-assembly logic used
        by both the explain_mission service (services.py) and its REST
        counterpart (api_views.py), so the two never drift out of sync.

        mission_id=None defaults to the most recent mission (whether
        anomalous or not — is_anomalous in the result tells the caller
        which case it is, so this doubles as a general "explain the last
        mission" call).

        record_override (v3.3.1, cloud-source Explain): when given, used
        directly as the record instead of looking one up via find_by_id()/
        latest() — mission_id is ignored in that case except as the
        result's echoed "mission_id" (read from record_override itself).
        This is how callers resolve a cloud-only synthetic id (e.g.
        "c_{ts}", which never exists in this store's _records) without
        MissionStore needing any knowledge of cloud_coordinator — the
        caller (api_views.py) does that resolution and hands over a
        plain record dict shaped for this method's field expectations
        (duration_min, area_sqft, dirt, error_code — see
        _cloud_record_to_explain_input() in api_views.py). recharge_min
        and npicks_delta are local-only telemetry and are legitimately
        absent on such records — anomaly_reason()/robot_lifted below
        already treat both as optional, so this isn't a special case.

        Returns None only when no matching mission exists at all (bad
        mission_id, or empty history) — callers translate that into
        whatever's appropriate for their transport (ServiceValidationError
        for the service, 404 for the REST view).
        """
        if record_override is not None:
            record = record_override
        elif mission_id is not None:
            record = self.find_by_id(mission_id)
        else:
            record = self.latest()
        if record is None:
            return None

        stats = self.compute_rolling_stats()
        if stats is None:
            stats = self.archive_baseline   # L3-ARC fallback, same as consecutive_anomalous

        reason = self.anomaly_reason(record, stats) if stats is not None else None
        npicks_delta = record.get("npicks_delta") or 0

        return {
            "mission_id": record.get("id"),
            "is_anomalous": reason is not None,
            "anomaly_reason": reason,
            "robot_lifted": npicks_delta > 0,
            "error_code": record.get("error_code"),
            "recommended_action": self._ANOMALY_RECOMMENDATIONS.get(reason),
        }

    # All result values that represent a stuck/failure event.
    STUCK_RESULTS: frozenset[str] = frozenset({
        "stuck", "stuck_and_resumed", "stuck_and_abandoned"
    })
    ERROR_RESULTS: frozenset[str] = frozenset({
        "error", "stuck", "stuck_and_resumed", "stuck_and_abandoned"
    })

    def query(
        self,
        days: int,
        result: str | frozenset[str] | None = None,
    ) -> list[dict[str, Any]]:
        """Return records from the last `days` days, optionally filtered by result.

        ``result`` may be a single string for exact match or a frozenset of
        strings for any-of matching.  Use ``MissionStore.STUCK_RESULTS`` to
        query all stuck variants at once.

        Records are returned sorted ascending by started_at.
        """
        result_set: frozenset[str] | None = (
            None if result is None else
            result if isinstance(result, frozenset) else
            frozenset({result})
        )
        cutoff = dt_util.now() - timedelta(days=days)
        out = []
        for r in self._records:
            started = dt_util.parse_datetime(r.get("started_at", ""))
            if started is None:
                continue
            # Make started timezone-aware for comparison if needed
            if started.tzinfo is None:
                started = started.replace(tzinfo=timezone.utc)
            if started < cutoff:
                continue
            if result_set is not None and r.get("result") not in result_set:
                continue
            out.append(r)
        return sorted(out, key=lambda r: r.get("started_at", ""))

    def query_by_error(
        self,
        error_code: int,
        days: int,
        zone: str | None = None,
    ) -> list[dict[str, Any]]:
        """Return records for a specific error code in the last `days` days.

        F8b — used by the error-recurrence Repair Issue to detect ≥3 occurrences
        of the same error code in the same zone within a lookback window.

        Args:
            error_code: The integer error code to match (cleanMissionStatus.error).
            days:       Lookback window in days.
            zone:       When set, additionally filter by the first zone name.
                        Pass None to return all records regardless of zone.
        """
        results = []
        for r in self.query(days):
            if r.get("error_code") != error_code:
                continue
            if zone is not None:
                zones = r.get("zones") or []
                if not zones or zones[0] != zone:
                    continue
            results.append(r)
        return results

    def query_by_day(self, days: int) -> dict[date, DaySummary]:
        """Group query(days) results by local calendar date.

        Returns a dict keyed by date. Days with no missions are omitted.
        """
        from collections import defaultdict
        buckets: dict[date, list[dict]] = defaultdict(list)
        for r in self.query(days):
            dt = dt_util.parse_datetime(r.get("started_at", ""))
            if dt:
                buckets[dt_util.as_local(dt).date()].append(r)

        summaries: dict[date, DaySummary] = {}
        for day, records in buckets.items():
            total = len(records)
            # F6h: stuck_and_resumed counts as completed (robot finished);
            # stuck_and_abandoned counts as stuck (mission failed).
            # F6c: blocked_timeout counts as total but not completed.
            completed = sum(
                1 for r in records
                if r.get("result") in ("completed", "stuck_and_resumed")
            )
            stuck = sum(
                1 for r in records
                if r.get("result") in ("stuck", "stuck_and_abandoned")
            )
            areas = [r["area_sqft"] for r in records if r.get("area_sqft") is not None]
            area_sqft: float | None = float(sum(areas)) if areas else None
            # Dominant result: stuck > error > completed > cancelled > none
            #
            # v3.2.1 — two fixes from card-development round 2.2:
            #   1. stuck now ranks ABOVE error.  A stuck robot needs physical
            #      intervention (wedged, cliff, lost) and is the more
            #      actionable signal for the card's day cell; a coded error
            #      often self-describes and frequently self-resolves.
            #   2. error_* variants ("error_17", "error_battery",
            #      "cancelled_by_user") reach the store via the import
            #      endpoint and rest980 migration despite the local enum
            #      convention (see B1-EXT note) — the exact string match
            #      missed them, so an error_battery-only day ranked "none".
            #      Prefix-match error_*, and count cancelled_by_user under
            #      cancelled (same fall-through class of bug).
            results = {r.get("result") for r in records}
            has_error = any(
                isinstance(res, str)
                and (res == "error" or res.startswith("error_"))
                for res in results
            )
            dominant = (
                "stuck" if results & {"stuck", "stuck_and_abandoned"} else
                "error" if has_error else
                "completed" if results & {"completed", "stuck_and_resumed"} else
                "cancelled" if results & {"cancelled", "cancelled_by_user"} else
                "none"
            )
            summaries[day] = DaySummary(
                date=day,
                total=total,
                completed=completed,
                stuck=stuck,
                area_sqft=area_sqft,
                result=dominant,
            )
        return summaries

    # ── v2.2.0 CR4 — timeline room-event extraction ───────────────────────────

    def _resolve_region_ids(
        self,
        rid_list: list[str],
        region_map: dict[str, str],
    ) -> list[str]:
        """Resolve region IDs to display names via region_map.

        Unknown IDs are returned as their raw ID string — data is never
        silently dropped. Name changes in the Smart Map are reflected
        automatically since region_map is passed at read time, not stored.
        """
        return [region_map.get(rid, rid) for rid in rid_list]

    @staticmethod
    def extract_rid(item: Any) -> str:
        """Extract a region ID from a plan.upcoming entry.

        Handles two confirmed formats:
        - String (some firmware/app versions): "23"
        - Object (lewis 22.52.10+): {"type": "rid", "rid": "23"}
          Also accepts {"region_id": "23"} as a fallback.

        Returns an empty string when neither format is recognisable so the
        caller can filter it out.
        """
        if isinstance(item, dict):
            return str(item.get("rid") or item.get("region_id") or "")
        return str(item) if item is not None else ""

    # v3.3.0 STORE-ENCAP — deprecated private alias. 18 test call sites
    # still use the old name; removal earmarked for SENSOR-SPLIT (v3.4.0)
    # when those test files are touched anyway. Production code must use
    # extract_rid (enforced by the encapsulation guard test).
    _extract_rid = extract_rid

    def latest_cleaned_rooms(
        self,
        region_map: dict[str, str],
        umf_regions: dict[str, str] | None = None,
    ) -> list[str] | None:
        """Return room names in cleaning-completion order from the most recent mission.

        CR4 — uses timeline.finEvents filtered to type='room' and status in (0, 6).
        status=0 = pass complete (normal).
        status=6 = pass complete after error recovery (lewis 22.52.10+ confirmed).
        status=1 = pass in progress — excluded.
        status=5 = interrupted by user/app — excluded.
        Each room appears once; the last qualifying event per rid is used.

        umf_regions: optional fallback for EPHEMERAL robots without coordinator.regions.
        When region_map is empty and umf_regions is provided, umf_regions is used.

        Returns None for whole-home missions (no 'room' events) or when
        the timeline field is absent (non-SMART robot or pre-merge record).
        """
        effective_map = region_map if region_map else (umf_regions or {})
        latest = self.latest()
        if latest is None:
            return None
        return self._record_room_names(latest, effective_map)

    def _record_room_names(
        self,
        rec: dict[str, Any],
        effective_map: dict[str, str],
    ) -> list[str] | None:
        """Room names cleaned in ONE record, completion order.

        v3.3.0 ROOM-SCHED foundation fix — the derivation half of
        latest_cleaned_rooms(), applicable to ANY record: live records
        never carry a stored `last_cleaned_rooms` field (they carry the
        cloud-merged `timeline`), so every reader of that field only
        ever worked for IMPORTED histories. Same bug class as the
        v3.1.1 last_mission_summary fix, one level deeper — this helper
        is the single shared derivation both room_cleaning_history()
        and room_coverage_health() now use.

        Order of sources:
        1. timeline.finEvents room events, status in (0, 6) — live path
        2. rec["last_cleaned_rooms"] — imported records keep working
        """
        timeline = rec.get("timeline")
        if isinstance(timeline, dict):
            fin_events = timeline.get("finEvents") or []
            seen: dict[str, int] = {}   # rid → index in ordered list
            ordered: list[str] = []
            for ev in fin_events:
                if not isinstance(ev, dict) or ev.get("type") != "room":
                    continue
                room = ev.get("room") or {}
                if room.get("status") not in (0, 6):   # 0=complete, 6=after-recovery
                    continue
                rid = str(room.get("rid", ""))
                if not rid:
                    continue
                if rid not in seen:
                    seen[rid] = len(ordered)
                    ordered.append(rid)
            if ordered:
                return self._resolve_region_ids(ordered, effective_map)
        imported = rec.get("last_cleaned_rooms")
        if isinstance(imported, list) and imported:
            return [str(r) for r in imported if isinstance(r, str)]
        return None

    def latest_planned_order(
        self,
        region_map: dict[str, str],
        umf_regions: dict[str, str] | None = None,
    ) -> list[str] | None:
        """Return room names in user-selected submission order.

        CR4 — uses timeline.plan.upcoming. Preserves the exact order the user
        selected rooms in the iRobot app. Returns None when absent or empty.

        umf_regions: optional fallback for EPHEMERAL robots (see latest_cleaned_rooms).
        """
        effective_map = region_map if region_map else (umf_regions or {})
        latest = self.latest()
        if latest is None:
            return None
        timeline = latest.get("timeline")
        if not isinstance(timeline, dict):
            return None
        upcoming = (timeline.get("plan") or {}).get("upcoming")
        if not upcoming:
            return None
        rids = [self._extract_rid(r) for r in upcoming]
        rids = [r for r in rids if r]   # drop empty strings from unrecognised formats
        if not rids:
            return None
        return self._resolve_region_ids(rids, effective_map)

    def latest_mission_destination(
        self,
        region_map: dict[str, str],
        umf_regions: dict[str, str] | None = None,
    ) -> str | None:
        """Return the last room in the planned order (final target of the job).

        Returns None for whole-home missions or when timeline is absent.

        umf_regions: optional fallback for EPHEMERAL robots (see latest_cleaned_rooms).
        """
        planned = self.latest_planned_order(region_map, umf_regions)
        return planned[-1] if planned else None

    def latest_room_coverage(
        self,
        region_map: dict[str, str],
        umf_regions: dict[str, str] | None = None,
    ) -> dict[str, float] | None:
        """Return per-room coverage fractions from the most recent mission.

        CR4 — uses room.totalArea / room.area from status=0 and status=6 events.
        totalArea = cumulative cleaned area across all passes (status=0/6 only).
        area = full room size (constant across events for the same rid).
        Values are clamped to [0.0, 1.0].

        Note: status=6 (completed after error recovery) events may not carry
        totalArea — such rooms are skipped from coverage but still appear in
        latest_cleaned_rooms(). This is expected behaviour for v2.2.

        umf_regions: optional fallback for EPHEMERAL robots (see latest_cleaned_rooms).

        Returns None when timeline is absent or no qualifying room events.
        """
        effective_map = region_map if region_map else (umf_regions or {})
        latest = self.latest()
        if latest is None:
            return None
        timeline = latest.get("timeline")
        if not isinstance(timeline, dict):
            return None
        fin_events = timeline.get("finEvents") or []
        coverage: dict[str, float] = {}
        for ev in fin_events:
            if ev.get("type") != "room":
                continue
            room = ev.get("room", {})
            if room.get("status") not in (0, 6):   # 0=complete, 6=complete-after-recovery
                continue
            rid = str(room.get("rid", ""))
            total_area = room.get("totalArea")
            area = room.get("area")
            if not rid or total_area is None or not area:
                continue
            try:
                fraction = min(1.0, max(0.0, float(total_area) / float(area)))
            except (ZeroDivisionError, TypeError, ValueError):
                continue
            coverage[rid] = fraction
        if not coverage:
            return None
        return {effective_map.get(rid, rid): frac for rid, frac in coverage.items()}

    def room_cleaning_history(
        self,
        region_map: dict[str, str] | None = None,
        umf_regions: dict[str, str] | None = None,
    ) -> dict[str, str]:
        """Return the most recent clean timestamp per room across all records.

        Scans records newest-first; the first hit per room name wins.
        v3.3.0 ROOM-SCHED foundation fix — room names are now DERIVED per
        record via _record_room_names() (timeline.finEvents on the live
        path, stored last_cleaned_rooms for imported records). Previously
        this read only the stored field, which no live pipeline ever
        writes — the method returned {} on every non-import installation
        (same bug class as the v3.1.1 last_mission_summary fix).

        region_map/umf_regions: same name-resolution pair as
        latest_cleaned_rooms(); without them, live-path rooms resolve to
        raw region IDs (still functional for interval math, just unnamed).

        Result: ``{"Kitchen": "2026-06-29T08:45:00", "Living Room": "2026-06-27T09:10:00"}``
        """
        effective_map = region_map if region_map else (umf_regions or {})
        result: dict[str, str] = {}
        for rec in reversed(self._records):
            ended_at = rec.get("ended_at")
            # v3.2.0 full-review fix — same defence-in-depth guard as
            # room_coverage_health: a non-string ended_at (poisoned
            # import from before the api_views.py type gate) must be
            # skipped here at the SOURCE, since this method's output
            # flows into room_coverage_health's parse_datetime calls.
            if not ended_at or not isinstance(ended_at, str):
                continue
            rooms = self._record_room_names(rec, effective_map) or []
            for room in rooms:
                if room not in result:
                    result[room] = ended_at
        return result

    def room_visit_counts(
        self,
        region_map: dict[str, str] | None = None,
        umf_regions: dict[str, str] | None = None,
    ) -> dict[str, int]:
        """v3.3.0 SMART-ORDER — number of recorded cleanings per room
        across the whole local window. Public accessor (STORE-ENCAP):
        the auto_clean_dirty_rooms service uses this for its
        min-missions trust gate on the L5 dirt index."""
        effective_map = region_map if region_map else (umf_regions or {})
        counts: dict[str, int] = {}
        for rec in self._records:
            for room in self._record_room_names(rec, effective_map) or []:
                counts[room] = counts.get(room, 0) + 1
        return counts

    def rooms_overdue_merged(
        self,
        config: dict[str, str] | None,
        now_iso: str,
        region_map: dict[str, str] | None = None,
        umf_regions: dict[str, str] | None = None,
    ) -> dict[str, dict[str, Any]]:
        """v3.3.0 ROOM-SCHED — the ONE home for the overdue rule, shared
        by sensor.*_rooms_overdue and the clean_overdue_rooms service
        (no duplicated rule set).

        Merge semantics (COVERAGE-FREQ -> ROOM-SCHED):
        - "configured": user set a frequency in the options flow ->
          expected interval from ROOM_SCHEDULE_INTERVALS, overdue at
          factor x1.0 (an explicit user expectation is a hard one).
        - "learned": no config -> self-calibrated interval and status
          from room_coverage_health() (its own x2.0 significance
          multiplier stands: the learned mean is noisy statistics, at
          x1.0 half of all rooms would permanently flag).
        - "insufficient_data": neither -> listed, never overdue.

        overdue_factor (days_since_last / expected) is included for the
        service's worst-first ordering; None without an interval.
        Rooms configured but never seen in the history are absent —
        days_since_last cannot be computed before the first clean.
        """
        from .const import ROOM_SCHEDULE_INTERVALS

        health = self.room_coverage_health(
            now_iso, region_map=region_map, umf_regions=umf_regions
        )
        config = config or {}
        result: dict[str, dict[str, Any]] = {}
        for room, info in health.items():
            days_since = info["days_since_last"]
            freq_key = config.get(room)
            if freq_key in ROOM_SCHEDULE_INTERVALS:
                expected: float | None = ROOM_SCHEDULE_INTERVALS[freq_key]
                source = "configured"
                status = "overdue" if days_since > expected else "healthy"
            elif info["expected_interval_days"] is not None:
                expected = info["expected_interval_days"]
                source = "learned"
                status = info["status"]
            else:
                expected = None
                source = "insufficient_data"
                status = "insufficient_data"
            result[room] = {
                "days_since_last": days_since,
                "expected_interval_days": (
                    round(expected, 2) if expected is not None else None
                ),
                "source": source,
                "status": status,
                "overdue_factor": (
                    round(days_since / expected, 2)
                    if expected and expected > 0 else None
                ),
            }
        return result

    def room_coverage_health(
        self,
        now_iso: str,
        days: int = 90,
        significance_multiplier: float = 2.0,
        min_intervals: int = 3,
        region_map: dict[str, str] | None = None,
        umf_regions: dict[str, str] | None = None,
    ) -> dict[str, dict[str, Any]]:
        """v3.2.0 COVERAGE-FREQ — is each room being cleaned in its OWN
        normal rhythm, self-calibrated per room rather than against a
        fixed global threshold.

        Reinterpreted from the original spec text ("GridStore EMA-decay
        curve -> recommended interval"): EMA decay is per-MISSION, not
        per-day, and converting it to a days-based recommendation would
        need an extra, somewhat arbitrary missions-per-day conversion
        step. Deriving the room's own historical cleaning cadence
        directly from its own timestamp gaps answers the same underlying
        question ("is this room being cleaned like it normally is")
        without that detour — and follows this project's established
        self-calibration philosophy (L9-BATTERY, L10, ROOM-ACCESS): a
        room is "overdue" only relative to ITS OWN typical interval, not
        an external one-size-fits-all number.

        For each room with a known last-clean timestamp (from
        room_cleaning_history()):
          - days_since_last: days since that room was last cleaned
          - expected_interval_days: mean gap between consecutive cleans
            of that specific room, from `last_cleaned_rooms` timestamps
            in the last `days` days. None when fewer than min_intervals
            gaps exist yet.
          - status: "overdue" when days_since_last exceeds the room's own
            mean + significance_multiplier * stdev (or mean * 1.5 as a
            fallback when there isn't enough variance data yet — same
            conservative-fallback spirit as L9-BATTERY's
            _ESTCAP_FALLBACK_MIN_RATE); "healthy" otherwise;
            "insufficient_data" when expected_interval_days is None.

        Returns {} when no room has ever been cleaned (no last_cleaned_rooms
        data at all — 600-series or a robot with no cloud enrichment yet).
        """
        cutoff = dt_util.now() - timedelta(days=days)
        effective_map = region_map if region_map else (umf_regions or {})
        visits_by_room: dict[str, list[str]] = {}
        for rec in self._records:
            ended_at = rec.get("ended_at")
            # v3.3.0 ROOM-SCHED foundation fix — derive, don't read the
            # never-written stored field (see room_cleaning_history).
            rooms: list[str] = self._record_room_names(rec, effective_map) or []
            # v3.2.0 full-review fix — defence in depth alongside the
            # import-gate type validation in api_views.py: a store that
            # was ALREADY poisoned with a non-string timestamp (imported
            # before that gate existed) must degrade to skipping the bad
            # record, not crash this method on every cloud-refresh cycle.
            if not ended_at or not isinstance(ended_at, str) or not rooms:
                continue
            ended_dt = dt_util.parse_datetime(ended_at)
            if ended_dt is None:
                continue
            if ended_dt.tzinfo is None:
                ended_dt = ended_dt.replace(tzinfo=timezone.utc)
            if ended_dt < cutoff:
                continue
            for room in rooms:
                visits_by_room.setdefault(room, []).append(ended_at)

        now_dt = dt_util.parse_datetime(now_iso)
        result: dict[str, dict[str, Any]] = {}

        for room, last_clean_iso in self.room_cleaning_history(region_map, umf_regions).items():
            last_dt = dt_util.parse_datetime(last_clean_iso)
            if now_dt is None or last_dt is None:
                continue
            if last_dt.tzinfo is None:
                last_dt = last_dt.replace(tzinfo=timezone.utc)
            days_since_last = (now_dt - last_dt).total_seconds() / 86400.0

            timestamps = sorted(visits_by_room.get(room, []))
            intervals: list[float] = []
            for i in range(len(timestamps) - 1):
                t1 = dt_util.parse_datetime(timestamps[i])
                t2 = dt_util.parse_datetime(timestamps[i + 1])
                if t1 is None or t2 is None:
                    continue
                gap_days = (t2 - t1).total_seconds() / 86400.0
                if gap_days > 0:
                    intervals.append(gap_days)

            if len(intervals) < min_intervals:
                result[room] = {
                    "days_since_last": round(days_since_last, 1),
                    "expected_interval_days": None,
                    "status": "insufficient_data",
                }
                continue

            mean_interval = statistics.mean(intervals)
            stdev_interval = (
                statistics.stdev(intervals) if len(intervals) >= 2 else 0.0
            )
            threshold = (
                mean_interval + significance_multiplier * stdev_interval
                if stdev_interval > 0
                else mean_interval * 1.5
            )
            status = "overdue" if days_since_last > threshold else "healthy"
            result[room] = {
                "days_since_last": round(days_since_last, 1),
                "expected_interval_days": round(mean_interval, 1),
                "status": status,
            }

        return result

    def presence_windows(self, days: int) -> list[MissionWindow]:
        """Return gaps between missions as potential 'all-away' windows.

        Each window spans from the end of one mission to the start of the next.
        resulted_in_clean=True when a completed mission started within the window.
        Used by L6 analytics sensors.
        """
        records = self.query(days)
        if len(records) < 2:
            return []

        windows: list[MissionWindow] = []
        completed_starts: set[str] = {
            r["started_at"] for r in records
            if r.get("result") in ("completed", "stuck_and_resumed")
        }

        for i in range(len(records) - 1):
            prev_end_str = records[i].get("ended_at", "")
            next_start_str = records[i + 1].get("started_at", "")
            prev_end = dt_util.parse_datetime(prev_end_str)
            next_start = dt_util.parse_datetime(next_start_str)
            if prev_end is None or next_start is None:
                continue
            # Make timezone-aware if needed
            if prev_end.tzinfo is None:
                prev_end = prev_end.replace(tzinfo=timezone.utc)
            if next_start.tzinfo is None:
                next_start = next_start.replace(tzinfo=timezone.utc)
            if next_start <= prev_end:
                continue
            duration_min = int((next_start - prev_end).total_seconds() / 60)
            resulted = next_start_str in completed_starts
            windows.append(MissionWindow(
                started_at=prev_end,
                ended_at=next_start,
                duration_min=duration_min,
                resulted_in_clean=resulted,
            ))
        return windows

    def clean_streak(self) -> int:
        """Return consecutive days ending today with at least one completed mission."""
        today = dt_util.now().date()
        completed_dates: set[date] = set()
        for r in self._records:
            if r.get("result") in ("completed", "stuck_and_resumed"):
                dt = dt_util.parse_datetime(r.get("started_at", ""))
                if dt:
                    completed_dates.add(dt_util.as_local(dt).date())
        streak = 0
        day = today
        while day in completed_dates:
            streak += 1
            day -= timedelta(days=1)
        return streak

    def p75_area(self, days: int) -> float | None:
        """Return the 75th-percentile area_sqft across records in the last `days` days.

        F5f — self-calibrating home coverage baseline.  After 10+ missions this
        stabilises as a reliable proxy for a full-clean area, filtering out
        partial cleans (low end) and zero-area failures.

        Returns None when fewer than 5 positive-area records exist (insufficient
        history to establish a stable baseline). Threshold lowered from 10 to 5
        so the sensor activates within a week of first install.
        """
        areas = [
            r["area_sqft"]
            for r in self.query(days)
            if r.get("area_sqft") is not None and r["area_sqft"] > 0
        ]
        if len(areas) < 5:
            return None
        areas.sort()
        return areas[int(len(areas) * 0.75)]

    def wear_data(self, days: int) -> list[dict[str, Any]]:
        """Return daily bbrun.hr snapshots for the last `days` days.

        Returns one entry per calendar day — the highest recorded bbrun_hr
        value for that day — sorted ascending by date. Used by L4 wear
        intelligence to compute daily wear rates without needing consecutive-
        record arithmetic in the sensor layer.

        Returns an empty list when fewer than 2 days of data exist (not enough
        to compute a meaningful rate).
        """
        buckets: dict[date, int] = {}
        for r in self.query(days):
            dt = dt_util.parse_datetime(r.get("started_at", ""))
            if dt and r.get("bbrun_hr") is not None:
                day = dt_util.as_local(dt).date()
                if day not in buckets or r["bbrun_hr"] > buckets[day]:
                    buckets[day] = r["bbrun_hr"]
        return sorted(
            [{"date": d.isoformat(), "bbrun_hr": hr} for d, hr in buckets.items()],
            key=lambda x: x["date"],
        )

    def wear_rate_since_reset(
        self,
        reset_hr: int,
        reset_at: str | None,
        current_hr: int,
    ) -> float | None:
        """Return average bbrun hours consumed per day since the last part reset.

        Returns None when:
        - reset_at is unknown (reset never recorded or store was empty)
        - current_hr has not advanced beyond reset_hr (robot not yet used)
        - fewer than 3 days have elapsed since reset (insufficient baseline)
        - reset_at cannot be parsed

        Never returns a negative value.
        """
        if reset_at is None or current_hr <= reset_hr:
            return None
        try:
            reset_dt = dt_util.parse_datetime(reset_at)
        except (ValueError, TypeError):
            return None
        if reset_dt is None:
            return None
        if reset_dt.tzinfo is None:
            reset_dt = reset_dt.replace(tzinfo=timezone.utc)
        days_elapsed = (dt_util.utcnow() - reset_dt).total_seconds() / 86400
        if days_elapsed < 3:
            return None
        rate = (current_hr - reset_hr) / days_elapsed
        return round(max(0.0, rate), 2)

    @staticmethod
    def _merge_cloud_fields(local: dict, cloud: dict) -> bool:
        """Copy missing analytics fields from a cloud record into a local record.

        Only copies when the local field is absent or None — never overwrites.

        B1 — error 224 result correction: when the cloud record reveals
        pauseId=224 and the local result is 'stuck', correct the result to
        'error'. Error 224 is not a physical stuck: the robot aborts in under
        a second and returns to dock (runM=0, sqft=0). The MQTT mission end
        packet may not carry the error code reliably on lewis firmware.

        B1-EXT (v2.10.2) — generalised result correction for two further
        cases where the local MQTT classification is demonstrably wrong once
        the cloud record arrives:

          done=='bat' (battery error) with local 'completed'/'stuck_and_resumed'
            → local result corrected to 'error'; error_code backfilled from
            pauseId if absent. The robot ran until the battery died — never a
            successful completion. Confirmed in the field: a real archive had
            completed:3 / stuck:0 for a day that contained an error_battery
            mission, because done=='bat' was never corrected locally.

          done_raw=='usrEnd' (user cancellation) with local 'completed'/
          'stuck_and_resumed'
            → local result corrected to 'cancelled'. 'stuck_and_resumed' means
            "stuck, self-recovered, finished" — but if the user then cancelled
            before the mission actually finished, that reading is wrong. The
            stuck event itself is preserved in nstuck_delta and self_recovered;
            only the final result changes. 'stuck_and_abandoned' is deliberately
            not touched: the robot stopped on its own, independently of any
            user action.

        Both B1-EXT corrections use generic 'error'/'cancelled' (not granular
        'error_battery'/'cancelled_by_user') — those granular values only exist
        in the cloud-side vocabulary and are not in the local result enum; using
        them here would create undocumented result strings that query_by_day(),
        compute_rolling_stats(), and the card's result renderer don't know about.

        Returns True if any field was written.
        """
        wrote = False

        # B1 — error 224 result correction (before generic scalar merge).
        pause_id = int(cloud.get("pauseId", 0) or 0)
        if pause_id == 224 and local.get("result") == "stuck":
            local["result"] = "error"
            if local.get("error_code") is None:
                local["error_code"] = 224
            wrote = True

        # B1-EXT — battery error / user cancellation result correction.
        done = (cloud.get("done") or "").strip()
        done_raw = (cloud.get("done_raw") or "").strip()
        local_result = local.get("result")

        if done == "bat" and local_result in ("completed", "stuck_and_resumed"):
            local["result"] = "error"
            if local.get("error_code") is None and pause_id > 0:
                local["error_code"] = pause_id
            wrote = True

        elif done_raw == "usrEnd" and local_result in ("completed", "stuck_and_resumed"):
            local["result"] = "cancelled"
            wrote = True

        for field in _CLOUD_MERGE_SCALAR:
            if local.get(field) is None and cloud.get(field) is not None:
                local[field] = cloud[field]
                wrote = True
        for field in _CLOUD_MERGE_ARRAY:
            if not local.get(field) and cloud.get(field):
                local[field] = cloud[field]
                wrote = True
        return wrote

    @staticmethod
    def _backfill_area_sqft(local: dict) -> bool:
        """Set local["area_sqft"] from local["sqft"] when the canonical
        field is still absent but the raw cloud field is present.

        "sqft" is the raw cloud field name, kept as-is via
        _CLOUD_MERGE_SCALAR; "area_sqft" is the canonical local name read
        by RobotProfileStore.update_mission_stats() and
        compute_rolling_stats(). The two must be kept in sync at every
        call site that merges cloud fields — see backfill_from_cloud()
        and merge_latest_from_cloud() for why this is a separate step
        rather than folded into _merge_cloud_fields()/_CLOUD_MERGE_SCALAR
        (the field NAME differs, "sqft" → "area_sqft", which that
        generic same-name copy loop cannot express).

        Returns True if area_sqft was written.
        """
        if local.get("area_sqft") is None and local.get("sqft") is not None:
            local["area_sqft"] = local["sqft"]
            return True
        return False

