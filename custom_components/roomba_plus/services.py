async def async_handle_clean_room(call: ServiceCall) -> None:
    """Handle the roomba_plus.clean_room service call.

    Resolves room names → region_ids, looks up the fresh user_pmapv_id from
    live MQTT state, and fires a region-targeted start command.

    Only works for MapCapability.SMART robots (i7 / s9 / j-series).
    """
    hass = call.hass
    entity_ids: list[str] = call.data["entity_id"]

    # CLEAN-ROOM-PER-ROOM-PASSES (v2.9.0): exactly one of room_name / room_passes.
    raw_room_name = call.data.get(ATTR_ROOM_NAME)
    raw_room_passes: list[dict] | None = call.data.get(ATTR_ROOM_PASSES)

    if raw_room_name is not None and raw_room_passes is not None:
        raise ServiceValidationError(
            "Provide either room_name or room_passes, not both.",
            translation_domain=DOMAIN,
            translation_key="room_name_and_room_passes_conflict",
        )
    if raw_room_name is None and raw_room_passes is None:
        raise ServiceValidationError(
            "Provide either room_name or room_passes.",
            translation_domain=DOMAIN,
            translation_key="room_name_or_room_passes_required",
        )

    if raw_room_passes is not None:
        room_names: list[str] = [r["name"] for r in raw_room_passes]
        # Per-room override, aligned by index with room_names/resolved order.
        # None where the caller didn't specify two_pass for that particular room.
        per_room_two_pass: list[bool | None] = [
            r.get(ATTR_TWO_PASS) for r in raw_room_passes
        ]
    else:
        room_names = (
            [raw_room_name] if isinstance(raw_room_name, str) else raw_room_name
        )
        per_room_two_pass = [None] * len(room_names)

    ordered: bool = call.data[ATTR_ORDERED]

    ent_reg = er.async_get(hass)

    for entity_id in entity_ids:
        entry = ent_reg.async_get(entity_id)
        if entry is None:
            raise ServiceValidationError(
                f"Entity {entity_id} not found",
                translation_domain=DOMAIN,
                translation_key="entity_not_found",
                translation_placeholders={"entity_id": entity_id},
            )

        config_entry: RoombaConfigEntry | None = hass.config_entries.async_get_entry(
            entry.config_entry_id
        )
        if config_entry is None:
            raise ServiceValidationError(
                f"No config entry for {entity_id}",
                translation_domain=DOMAIN,
                translation_key="config_entry_not_found",
                translation_placeholders={"entity_id": entity_id},
            )

        data: RoombaData = config_entry.runtime_data

        if data.map_capability != MapCapability.SMART:
            raise ServiceValidationError(
                f"{entity_id} does not support Smart Map room cleaning. "
                "This requires a robot with a finalized Smart Map "
                "(i7, s9, j-series, or Braava jet m6). If you have a "
                "compatible robot, make sure its map is saved in the iRobot app.",
                translation_domain=DOMAIN,
                translation_key="not_smart_map",
            )

        zone_data: dict = config_entry.options.get(CONF_SMART_ZONE_DATA, {})

        if data.has_cloud:
            cc = data.cloud_coordinator
            cloud_zone_data: dict = {
                str(r["id"]): {"name": r["name"], "pmap_id": r["pmap_id"]}
                for r in cc.regions
                if r.get("id") and r.get("name")
            }
            cloud_zone_data.update({
                str(z["id"]): {"name": z["name"], "pmap_id": z["pmap_id"]}
                for z in cc.zones
                if z.get("id") and z.get("name")
            })
            zone_data = {**zone_data, **cloud_zone_data}

        if not zone_data:
            raise ServiceValidationError(
                "No rooms configured yet. Run a room-targeted clean via the "
                "iRobot app first, then assign names in the Roomba+ options.",
                translation_domain=DOMAIN,
                translation_key="no_rooms_configured",
            )

        state = data.roomba_reported_state()

        cloud_pmap_id: str | None = None
        if data.has_cloud:
            cloud_pmap_id = data.cloud_coordinator.active_pmap_id
            if cloud_pmap_id:
                _LOGGER.debug(
                    "clean_room: active pmap_id=%s (source=cloud) for %s",
                    cloud_pmap_id[:12], entity_id,
                )

        not_ready: int = (state.get("cleanMissionStatus") or {}).get("notReady", 0)
        if not_ready & MAP_UPDATING_NOT_READY_BIT:
            raise ServiceValidationError(
                "The robot is currently updating its Smart Map. "
                "Wait for the update to complete (readiness sensor shows 'Ready'), "
                "then try again.",
                translation_domain=DOMAIN,
                translation_key="map_updating",
            )

        resolved = _resolve_rooms(zone_data, room_names, state, cloud_pmap_id)
        if not resolved:
            # room_passes=[] (empty array) passes the "exactly one of" checks
            # above and _resolve_rooms returns [] without raising (it only
            # raises on *unknown* names, not an empty list). Guard the [0]
            # access explicitly.
            raise ServiceValidationError(
                "No rooms to clean — provide at least one room name.",
                translation_domain=DOMAIN,
                translation_key="no_rooms_resolved",
            )
        pmap_id = resolved[0][1]

        # v2.7.4 (PMAP-PMAPV): cloud coordinator is authoritative for lewis
        # firmware — lewis 22.52.10 does not broadcast pmaps updates via local
        # MQTT after map changes, so state.pmaps holds a stale/in-flux version.
        # Cloud last_user_pmapv_id is the stable committed token the robot
        # accepts. _resolve_pmapv_id provides lastCommand/state.pmaps fallback
        # for robots without cloud credentials.
        user_pmapv_id: str = (
            (data.cloud_coordinator.active_user_pmapv_id if data.has_cloud else None)
            or _resolve_pmapv_id(state, pmap_id)
            or ""
        )

        # Read cleaning pass mode from live robot state (same source as CleaningPassesSelect)
        no_auto = bool(state.get("noAutoPasses", False))
        # v2.7.3: optional two_pass parameter overrides the robot's current state.
        # None = inherit from robot state (default); True/False = explicit override.
        # CLEAN-ROOM-PER-ROOM-PASSES (v2.9.0): per-room override (room_passes)
        # takes priority over the global two_pass override, which in turn
        # takes priority over the robot's current state. Resolved independently
        # per room, so a partial room_passes list (some rooms specify two_pass,
        # others don't) still resolves sensibly.
        caller_two_pass: bool | None = call.data.get(ATTR_TWO_PASS)
        robot_two_pass = bool(state.get("twoPass", False))

        def _resolve_two_pass(per_room: bool | None) -> bool:
            if per_room is not None:
                return per_room
            if caller_two_pass is not None:
                return caller_two_pass
            return robot_two_pass

        params = {
            "ordered": 1 if ordered else 0,
            "pmap_id": pmap_id,
            "user_pmapv_id": user_pmapv_id,
            "regions": [
                {
                    "region_id": rid,
                    "type": "rid",
                    "params": {
                        "noAutoPasses": no_auto,
                        "twoPass": _resolve_two_pass(
                            per_room_two_pass[i] if i < len(per_room_two_pass) else None
                        ),
                    },
                }
                for i, (rid, _) in enumerate(resolved)
            ],
        }

        _LOGGER.info(
            "clean_room: %s → regions=%s pmap=%s pmapv=%s",
            entity_id,
            [rid for rid, _ in resolved],
            pmap_id[:12],
            user_pmapv_id[:12] if user_pmapv_id else "none",
        )

        await hass.async_add_executor_job(
            data.roomba.send_command, "start", params
        )

    return {}


async def async_handle_smart_start(call: ServiceCall) -> None:
    """Handle roomba_plus.smart_start with blocking-sensor gate."""
    hass = call.hass
    entity_ids: list[str] = call.data.get("entity_id", [])
    if isinstance(entity_ids, str):
        entity_ids = [entity_ids]
    rooms: list[str] | None = call.data.get(ATTR_ROOMS)
    override: bool = call.data.get(ATTR_OVERRIDE_BLOCKING, False)

    ent_reg = er.async_get(hass)

    for eid in entity_ids:
        entry_reg = ent_reg.async_get(eid)
        if entry_reg is None:
            raise ServiceValidationError(
                f"Entity {eid} not found",
                translation_domain=DOMAIN,
                translation_key="entity_not_found",
            )
        config_entry = hass.config_entries.async_get_entry(entry_reg.config_entry_id)
        if config_entry is None:
            raise ServiceValidationError(
                f"No config entry for {eid}",
                translation_domain=DOMAIN,
                translation_key="config_entry_not_found",
                translation_placeholders={"entity_id": eid},
            )

        data: RoombaData = config_entry.runtime_data
        if rooms and data.map_capability != MapCapability.SMART:
            raise ServiceValidationError(
                f"{eid} does not support room targeting — this requires a "
                "robot with a finalized Smart Map (i7, s9, j-series, or "
                "Braava jet m6).",
                translation_domain=DOMAIN,
                translation_key="not_smart_map",
            )
        if data.blocking_manager is not None:
            await data.blocking_manager.check_and_start(rooms, override)
        elif rooms:
            await hass.services.async_call(
                DOMAIN,
                SERVICE_CLEAN_ROOM,
                {"entity_id": eid, ATTR_ROOM_NAME: rooms},
                blocking=True,
            )
        else:
            await hass.async_add_executor_job(data.roomba.start)


async def async_handle_clean_overdue_rooms(call: ServiceCall) -> None:
    """v3.3.0 ROOM-SCHED — roomba_plus.clean_overdue_rooms.

    Determines overdue rooms via the SAME merge rule as the
    rooms_overdue sensor (MissionStore.rooms_overdue_merged — one shared
    rule set), sorts worst-first by overdue_factor, and delegates the
    actual start to roomba_plus.clean_room (reusing its full pmap/
    resolution/notReady logic instead of duplicating it — the same
    delegation pattern smart_start uses).

    - SMART only: EPHEMERAL raises — the hardware cannot room-target
      (`pmap_id=None` on every EPHEMERAL mission, v3.2.1 ZoneSelect
      lesson).
    - Nothing overdue: deliberate NO-OP with an INFO log, not an error —
      automations may fire this daily without guards.
    - max_rooms (optional): cap, worst-first.
    """
    hass = call.hass
    entity_ids: list[str] = call.data.get("entity_id", [])
    if isinstance(entity_ids, str):
        entity_ids = [entity_ids]
    max_rooms: int | None = call.data.get("max_rooms")

    ent_reg = er.async_get(hass)
    for eid in entity_ids:
        entry_reg = ent_reg.async_get(eid)
        if entry_reg is None:
            raise ServiceValidationError(
                f"Entity {eid} not found",
                translation_domain=DOMAIN,
                translation_key="entity_not_found",
            )
        config_entry = hass.config_entries.async_get_entry(entry_reg.config_entry_id)
        if config_entry is None:
            raise ServiceValidationError(
                f"No config entry for {eid}",
                translation_domain=DOMAIN,
                translation_key="config_entry_not_found",
                translation_placeholders={"entity_id": eid},
            )
        data: RoombaData = config_entry.runtime_data
        if data.map_capability != MapCapability.SMART:
            raise ServiceValidationError(
                f"{eid} does not support room targeting — this requires a "
                "robot with a finalized Smart Map. EPHEMERAL-tier robots "
                "(900-series) cannot clean individual rooms.",
                translation_domain=DOMAIN,
                translation_key="not_smart_map",
            )
        ms = data.mission_store
        if ms is None:
            continue

        # Bug-hunt round 4 (seam check) — SAME name resolution as the
        # rooms_overdue sensor, incl. the UMF fallback: otherwise a
        # degraded cloud-regions window makes the sensor report overdue
        # rooms the service cannot resolve. Lazy import — no cycle
        # (sensor never imports services).
        from .sensor import _region_maps_for
        region_map, umf_regions = _region_maps_for(data)
        config = config_entry.options.get(CONF_ROOM_SCHEDULE) or {}
        merged = ms.rooms_overdue_merged(
            config, dt_util.now().isoformat(),
            region_map=region_map, umf_regions=umf_regions,
        )
        overdue = sorted(
            (n for n, i in merged.items() if i["status"] == "overdue"),
            key=lambda n: merged[n]["overdue_factor"] or 0,
            reverse=True,
        )
        if max_rooms:
            overdue = overdue[: int(max_rooms)]
        if not overdue:
            _LOGGER.info(
                "clean_overdue_rooms: nothing overdue for %s — no-op", eid
            )
            continue
        overdue = _route_optimize_order(data, overdue, region_map)
        _LOGGER.info(
            "clean_overdue_rooms: starting %s for %s "
            "(overdue-selected, route-ordered)",
            overdue, eid,
        )
        await hass.services.async_call(
            DOMAIN,
            SERVICE_CLEAN_ROOM,
            {"entity_id": eid, ATTR_ROOM_NAME: overdue},
            blocking=True,
        )


def _route_optimize_order(
    data: "RoombaData",
    room_names: list[str],
    region_map: dict[str, str],
) -> list[str]:
    """v3.3.0 SMART-ORDER routing — reorder the SELECTED rooms by travel
    path: greedy nearest-neighbour over the UMF room centroids, starting
    at the dock (pose origin (0,0) mapped via pose_to_umf). iRobot works
    the submitted region list IN ORDER, so a dirt-/overdue-sorted list
    can mean criss-crossing the home.

    Deliberate split of concerns: dirt/overdue decide WHICH rooms (and
    the max_rooms cap — the dirtiest room must never lose to the cap
    just for being far away); geometry only decides the ORDER of the
    already-selected set. Greedy NN is practically optimal at the
    typical 2–6 rooms; a real TSP would be overengineering.

    Fallbacks (input order returned unchanged): aligner missing or not
    aligned, dock not mappable, fewer than 3 rooms with centroids.
    Rooms without a centroid keep their relative order at the end.
    """
    if len(room_names) < 2:
        return room_names
    aligner = getattr(data, "umf_aligner", None)
    if aligner is None or not getattr(aligner, "aligned", False):
        return room_names
    try:
        centroids_by_rid = aligner.room_centroids_umf()
        dock = aligner.pose_to_umf(0.0, 0.0)
    except Exception:  # noqa: BLE001 — routing is a bonus, never a blocker
        return room_names
    if not centroids_by_rid or dock is None:
        return room_names

    name_to_rid = {name: rid for rid, name in region_map.items()}
    positioned = [
        (n, centroids_by_rid[name_to_rid[n]])
        for n in room_names
        if name_to_rid.get(n) in centroids_by_rid
    ]
    unpositioned = [n for n in room_names if n not in {p[0] for p in positioned}]
    if len(positioned) < 2:
        return room_names

    ordered: list[str] = []
    cx, cy = dock
    remaining = positioned[:]
    while remaining:
        i = min(
            range(len(remaining)),
            key=lambda k: (remaining[k][1][0] - cx) ** 2
            + (remaining[k][1][1] - cy) ** 2,
        )
        name, (cx, cy) = remaining.pop(i)
        ordered.append(name)
    return ordered + unpositioned


_SMART_ORDER_MIN_MISSIONS = 10  # v3.3.0 SMART-ORDER — index trust gate


async def async_handle_auto_clean_dirty_rooms(call: ServiceCall) -> None:
    """v3.3.0 SMART-ORDER — roomba_plus.auto_clean_dirty_rooms.

    Targeted clean of the household's dirty rooms, sorted DESCENDING by
    the L5 per-room dirt index (EMA of passCount/m²). A room qualifies
    when:
    - it has >= _SMART_ORDER_MIN_MISSIONS recorded cleanings (the EMA is
      not trustworthy on thin data), AND
    - its relative dirt (room_dirt_relative) is >= 1.0 — at or above
      the household average. Below-average rooms are not "dirty rooms".

    No qualifying room -> WHOLE-HOUSE clean as the fallback (per spec):
    thin data means "clean normally", not "refuse".
    SMART-only like clean_overdue_rooms: on EPHEMERAL the dirt index has
    no per-room data source and targeting is impossible — raising is
    more honest than silently degrading to a plain start.
    Delegates targeted starts to clean_room (shared pmap/resolution
    logic, same pattern as smart_start/clean_overdue_rooms).
    """
    hass = call.hass
    entity_ids: list[str] = call.data.get("entity_id", [])
    if isinstance(entity_ids, str):
        entity_ids = [entity_ids]
    max_rooms: int | None = call.data.get("max_rooms")

    ent_reg = er.async_get(hass)
    for eid in entity_ids:
        entry_reg = ent_reg.async_get(eid)
        if entry_reg is None:
            raise ServiceValidationError(
                f"Entity {eid} not found",
                translation_domain=DOMAIN,
                translation_key="entity_not_found",
            )
        config_entry = hass.config_entries.async_get_entry(entry_reg.config_entry_id)
        if config_entry is None:
            raise ServiceValidationError(
                f"No config entry for {eid}",
                translation_domain=DOMAIN,
                translation_key="config_entry_not_found",
                translation_placeholders={"entity_id": eid},
            )
        data: RoombaData = config_entry.runtime_data
        if data.map_capability != MapCapability.SMART:
            raise ServiceValidationError(
                f"{eid} does not support room targeting — this requires a "
                "robot with a finalized Smart Map. EPHEMERAL-tier robots "
                "(900-series) cannot clean individual rooms.",
                translation_domain=DOMAIN,
                translation_key="not_smart_map",
            )
        ms = data.mission_store
        rps = getattr(data, "robot_profile_store", None)

        # Bug-hunt round 4 (seam check) — same resolution incl. UMF
        # fallback as the sensor/overdue paths.
        from .sensor import _region_maps_for
        _rm, _umf = _region_maps_for(data)
        region_map: dict[str, str] = _rm or (_umf or {})

        candidates: list[str] = []
        if ms is not None and rps is not None:
            visit_counts = ms.room_visit_counts(region_map)
            relative = rps.room_dirt_relative()          # rid-keyed
            index = dict(rps.room_dirt_index)            # rid-keyed
            qualifying = []
            for rid, rel in relative.items():
                name = region_map.get(rid, rid)
                if rel < 1.0:
                    continue
                if visit_counts.get(name, 0) < _SMART_ORDER_MIN_MISSIONS:
                    continue
                qualifying.append((index.get(rid, 0.0), name))
            qualifying.sort(reverse=True)                # dirtiest first
            candidates = [name for _, name in qualifying]

        if max_rooms:
            candidates = candidates[: int(max_rooms)]

        if not candidates:
            _LOGGER.info(
                "auto_clean_dirty_rooms: no room passes the dirt-index "
                "trust gate for %s — whole-house fallback", eid,
            )
            await hass.async_add_executor_job(data.roomba.start)
            continue

        candidates = _route_optimize_order(data, candidates, region_map)
        _LOGGER.info(
            "auto_clean_dirty_rooms: starting %s for %s "
            "(dirt-selected, route-ordered)",
            candidates, eid,
        )
        await hass.services.async_call(
            DOMAIN,
            SERVICE_CLEAN_ROOM,
            {"entity_id": eid, ATTR_ROOM_NAME: candidates},
            blocking=True,
        )


