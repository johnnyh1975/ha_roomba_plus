def _async_signal_entities(
    hass: HomeAssistant,
    entry_id: str,
    keys: list[str],
) -> None:
    """Force-refresh sensor/binary_sensor entities after non-MQTT state changes.

    Called after maintenance resets to immediately reflect the new baseline
    in timestamp sensors and L4 wear sensors, without waiting for the next
    MQTT push from the robot.
    """
    ent_reg = er.async_get(hass)
    entry = hass.config_entries.async_get_entry(entry_id)
    if entry is None:
        return
    data: RoombaData = entry.runtime_data
    robot_uid = f"roomba_plus_{data.blid}"
    for key in keys:
        for platform in ("sensor", "binary_sensor"):
            entity_id = ent_reg.async_get_entity_id(
                platform, DOMAIN, f"{robot_uid}_{key}"
            )
            if entity_id:
                current = hass.states.get(entity_id)
                if current:
                    hass.states.async_set(
                        entity_id,
                        current.state,
                        current.attributes,
                        force_update=True,
                    )
                break


def _fire_maintenance_reset_event(
    hass: HomeAssistant,
    config_entry: "RoombaConfigEntry",
    component: str,
    hours: int | None,
) -> None:
    """v2.9.0 LOGBOOK — fire roomba_plus_maintenance_reset.

    Shared by BOTH call paths that perform a maintenance reset: the
    roomba_plus.reset_* services (this module, below) and the Filter/Brush/
    Battery reset buttons (button.py — imports this helper, same pattern
    as button.py's existing import of _resolve_pmapv_id from here). One
    event regardless of which path the user took, so the Logbook entry
    appears consistently either way.

    hours is None for the calendar-based inspect resets (wheel/contact/bin
    cleaning) — those have no hr-counter baseline to report.
    """
    hass.bus.async_fire(
        EVENT_MAINTENANCE_RESET,
        {
            "entry_id": config_entry.entry_id,
            "name": config_entry.title,
            "component": component,
            "hours": hours,
        },
    )


