async def async_handle_clean_sequence(
    hass: HomeAssistant, call: ServiceCall
) -> None:
    """F10d — start robot B when robot A mission ends.

    Subscribes a one-shot state change listener on robot A's vacuum entity.
    When A transitions to 'docked' (completed mission), optionally waits
    delay_minutes, then calls vacuum.start on robot B.

    The listener removes itself after firing once regardless of outcome.

    Args:
        entity_id:         Trigger robot — must finish first.
        target_entity_id:  Target robot — started after trigger.
        require_completed: When True (default), robot B starts only if A docked.
                           When False, B starts even if A is idle/error.
        delay_minutes:     Wait this many minutes after A docks before starting B.
    """
    from homeassistant.core import callback
    from homeassistant.helpers import entity_registry as er

    trigger_entity = call.data["entity_id"]
    target_entity  = call.data["target_entity_id"]
    require_completed = call.data.get("require_completed", True)
    delay_minutes     = int(call.data.get("delay_minutes", 0))

    # Validate both entities belong to roomba_plus vacuum platform
    reg = er.async_get(hass)
    for eid in (trigger_entity, target_entity):
        entry = reg.async_get(eid)
        if entry is None or entry.platform != DOMAIN or entry.domain != "vacuum":
            raise vol.Invalid(
                f"Entity {eid} is not a roomba_plus vacuum entity"
            )

    _LOGGER.debug(
        "clean_sequence: registered — trigger=%s target=%s "
        "require_completed=%s delay=%dmin",
        trigger_entity, target_entity, require_completed, delay_minutes,
    )

    unsub_ref: list = []

    @callback
    def _on_trigger_state_change(
        entity_id: str, old_state: Any, new_state: Any
    ) -> None:
        if new_state is None:
            return
        state_str = new_state.state
        should_fire = (
            state_str == "docked"
            or (state_str in ("idle", "error") and not require_completed)
        )
        if should_fire:
            if delay_minutes > 0:
                hass.async_create_task(
                    _delayed_start(delay_minutes),
                    name=f"roomba_plus_clean_sequence_{target_entity}",
                )
            else:
                hass.async_create_task(
                    hass.services.async_call(
                        "vacuum", "start", {"entity_id": target_entity}
                    ),
                    name=f"roomba_plus_clean_sequence_imm_{target_entity}",
                )
            if unsub_ref:
                unsub_ref[0]()

    async def _delayed_start(delay_min: int) -> None:
        import asyncio as _asyncio
        await _asyncio.sleep(delay_min * 60)
        await hass.services.async_call(
            "vacuum", "start", {"entity_id": target_entity}
        )
        _LOGGER.info(
            "clean_sequence: started %s after %dmin delay (trigger: %s)",
            target_entity, delay_min, trigger_entity,
        )

    from homeassistant.helpers.event import async_track_state_change
    unsub = async_track_state_change(hass, trigger_entity, _on_trigger_state_change)
    unsub_ref.append(unsub)


# ── Registration ──────────────────────────────────────────────────────────────

def async_register_services(hass: HomeAssistant) -> None:
    """Register all Roomba+ domain services. Safe to call multiple times.

    Each service is guarded by has_service() — only registers on first call,
    subsequent entries reuse the same handlers.
    """
    _RESET_SCHEMA = vol.Schema({vol.Required("entity_id"): cv.entity_ids})

    if not hass.services.has_service(DOMAIN, SERVICE_CLEAN_ROOM):
        hass.services.async_register(
            DOMAIN,
            SERVICE_CLEAN_ROOM,
            async_handle_clean_room,
            schema=vol.Schema({
                vol.Required("entity_id"): cv.entity_ids,
                # CLEAN-ROOM-PER-ROOM-PASSES (v2.9.0): room_name is now optional
                # at the schema level — the handler enforces "exactly one of
                # room_name / rooms" so the error message can be specific.
                vol.Optional(ATTR_ROOM_NAME): vol.Any(
                    cv.string,
                    vol.All(cv.ensure_list, [cv.string]),
                ),
                # Bugfix (found while implementing CLEAN-ROOM-PER-ROOM-PASSES):
                # two_pass was read by the handler and documented in
                # services.yaml, but missing from this schema entirely — any
                # caller passing it via hass.services.async_call() with
                # validation enabled (the normal path for YAML automations
                # and the Developer Tools UI) was rejected with "extra keys
                # not allowed @ data['two_pass']". Untested — test_services.py
                # had no coverage that went through real schema validation.
                vol.Optional(ATTR_TWO_PASS): cv.boolean,
                vol.Optional(ATTR_ORDERED, default=True): cv.boolean,
                # CLEAN-ROOM-PER-ROOM-PASSES (v2.9.0): individual pass count
                # per room within the same sequence, e.g.
                #   room_passes: [{name: Kitchen, two_pass: true}, {name: Hallway}]
                vol.Optional(ATTR_ROOM_PASSES): vol.All(cv.ensure_list, [
                    vol.Schema({
                        vol.Required("name"): cv.string,
                        vol.Optional(ATTR_TWO_PASS): cv.boolean,
                    })
                ]),
            }),
            supports_response=SupportsResponse.OPTIONAL,
        )
        _LOGGER.debug("Registered %s.%s action", DOMAIN, SERVICE_CLEAN_ROOM)

    if not hass.services.has_service(DOMAIN, SERVICE_SMART_START):
        hass.services.async_register(
            DOMAIN,
            SERVICE_SMART_START,
            async_handle_smart_start,
            schema=vol.Schema({
                vol.Required("entity_id"): cv.entity_ids,
                vol.Optional(ATTR_ROOMS): vol.All(cv.ensure_list, [cv.string]),
                vol.Optional(ATTR_OVERRIDE_BLOCKING, default=False): cv.boolean,
            }),
        )
        _LOGGER.debug("Registered %s.%s action", DOMAIN, SERVICE_SMART_START)

    for _part in ("filter", "brush", "battery", "pad"):
        svc = f"reset_{_part}"
        if not hass.services.has_service(DOMAIN, svc):
            _p = _part

            async def _make_reset_handler(
                call: ServiceCall, p: str = _p
            ) -> None:
                await _handle_reset_service(call.hass, call, p)

            hass.services.async_register(
                DOMAIN,
                svc,
                _make_reset_handler,
                schema=_RESET_SCHEMA,
            )

    # ── IA74-MAINT (v2.7.0) — calendar-based inspect resets ──────────────────
    for _component in ("wheel", "contact", "bin"):
        svc = f"reset_{_component}_cleaning"
        if not hass.services.has_service(DOMAIN, svc):
            _c = _component

            async def _make_inspect_handler(
                call: ServiceCall, c: str = _c
            ) -> None:
                await _handle_inspect_reset_service(call.hass, call, c)

            hass.services.async_register(
                DOMAIN,
                svc,
                _make_inspect_handler,
                schema=_RESET_SCHEMA,
            )

    if not hass.services.has_service(DOMAIN, "reset_robot_profile"):
        hass.services.async_register(
            DOMAIN,
            "reset_robot_profile",
            async_handle_reset_robot_profile,
            schema=_RESET_SCHEMA,
        )

    # ── F10d — clean_sequence ─────────────────────────────────────────────────
    if not hass.services.has_service(DOMAIN, SERVICE_CLEAN_SEQUENCE):
        hass.services.async_register(
            DOMAIN,
            SERVICE_CLEAN_SEQUENCE,
            async_handle_clean_sequence,
            schema=_CLEAN_SEQUENCE_SCHEMA,
        )
        _LOGGER.debug("Registered %s.%s action", DOMAIN, SERVICE_CLEAN_SEQUENCE)

    # ── ADVANCE-ROOM-V2 (v2.8.0) — manual room-progress override ─────────────
    if not hass.services.has_service(DOMAIN, "advance_room"):
        hass.services.async_register(
            DOMAIN,
            "advance_room",
            async_handle_advance_room,
            schema=vol.Schema({vol.Required("entity_id"): cv.entity_ids}),
        )
        _LOGGER.debug("Registered %s.advance_room action", DOMAIN)


async def async_handle_advance_room(call: ServiceCall) -> None:
    """Handle roomba_plus.advance_room — manual room-progress override.

    ADVANCE-ROOM-V2 (v2.8.0) — Lewis firmware robots send a non-run phase
    event at every inter-room boundary. Occasionally the progress sensor gets
    stuck on a completed room. This service lets the user manually advance
    current_room_idx by one step.

    Guards:
      1. MissionTimerStore must exist (SMART + cloud robots only).
      2. An active mission must be in progress (mission_id not None).
      3. Robot must NOT be in phase=run (only advance during transitions).
      4. current_room_idx must be < len(planned_rooms) - 1.
    """
    hass = call.hass
    ent_reg = er.async_get(hass)

    for entity_id in call.data["entity_id"]:
        entry = ent_reg.async_get(entity_id)
        if entry is None:
            raise ServiceValidationError(
                f"Entity {entity_id} not found",
                translation_domain=DOMAIN,
                translation_key="entity_not_found",
                translation_placeholders={"entity_id": entity_id},
            )

        config_entry: RoombaConfigEntry | None = hass.config_entries.async_get_entry(
            entry.config_entry_id
        )
        if config_entry is None:
            raise ServiceValidationError(
                f"No config entry for {entity_id}",
                translation_domain=DOMAIN,
                translation_key="config_entry_not_found",
                translation_placeholders={"entity_id": entity_id},
            )

        data: RoombaData = config_entry.runtime_data
        mts = data.mission_timer_store

        # Guard 1: MissionTimerStore only exists for SMART + cloud robots
        if mts is None:
            raise ServiceValidationError(
                f"{entity_id}: advance_room requires a SMART map robot with "
                "cloud credentials (i7/s9/j-series + iRobot account).",
                translation_domain=DOMAIN,
                translation_key="not_smart_map",
            )

        # Guard 2: active mission
        if mts.mission_id is None:
            _LOGGER.debug(
                "advance_room: no active mission for %s — ignored", entity_id
            )
            continue

        # Guard 3: non-run phase (don't advance while actively cleaning)
        state = data.roomba_reported_state()
        phase = (state.get("cleanMissionStatus") or {}).get("phase", "")
        if phase == "run":
            _LOGGER.debug(
                "advance_room: robot in phase=run for %s — ignored (advance "
                "during transition phase only)",
                entity_id,
            )
            continue

        # Guard 4 + actual advance (returns False if already at last room)
        advanced = mts.advance_room(hass, config_entry.entry_id)
        if advanced:
            _LOGGER.info(
                "advance_room: advanced to room %d/%d (%s) for %s",
                mts.current_room_idx + 1,
                len(mts.planned_rooms),
                mts.current_room,
                entity_id,
            )
        else:
            _LOGGER.debug(
                "advance_room: already at last room for %s — ignored", entity_id
            )


def async_remove_services(hass: HomeAssistant) -> None:
    """Remove all Roomba+ domain services (called when last entry unloads)."""
    for svc in (
        SERVICE_CLEAN_ROOM,
        SERVICE_SMART_START,
        SERVICE_CLEAN_SEQUENCE,
        "reset_filter",
        "reset_brush",
        "reset_battery",
        "reset_pad",
        "reset_wheel_cleaning",
        "reset_contact_cleaning",
        "reset_bin_cleaning",
        "reset_robot_profile",
        "advance_room",
    ):
        if hass.services.has_service(DOMAIN, svc):
            hass.services.async_remove(DOMAIN, svc)
